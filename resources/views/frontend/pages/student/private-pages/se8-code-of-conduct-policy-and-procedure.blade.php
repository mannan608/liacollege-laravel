                    <h2><span style="color: #da591f;">Confidentiality Procedure</span></h2>
                    <h3><span style="color: #fcb53b;">Compliance</span></h3>
                    <p>All employees and management are expected to:</p>
                    <ul>
                        <li>observe all policies, procedures, rules and regulations at all times</li>
                        <li>comply with all federal, state and local laws and regulations</li>
                        <li>comply with all reasonable, lawful instructions and decisions related to their work</li>
                        <li>maintain a high degree of ethics, integrity, honesty and professionalism in dealing with
                            community members and other employees</li>
                        <li>adhere to Workplace Health and Safety (WHS) Policy and Procedures</li>
                        <li>maintain the confidentiality of the service’s operations in relation to service activities,
                            confidential documentation and work practices during and after their employment</li>
                        <li>take reasonable steps to ensure their own health, safety and welfare in the workplace, as well
                            as that of other employees and community members – employees are expected to make themselves
                            familiar with their workplace health and safety obligations.</li>
                    </ul>
                    <h3><span style="color: #fcb53b;">Employee and Management Behaviour</span></h3>
                    <ul>
                        <li>If an employee breaches the following guidelines, disciplinary action may be taken.</li>
                        <li>If the breach of conduct is of a legal nature, it will be addressed in accordance with relevant
                            federal, state or local government laws.</li>
                        <li>Employees and management should not:
                            <ul>
                                <li>discriminate against another employee or community member on the basis of sex, age,
                                    race, religion, disability, pregnancy, marital status or sexual orientation</li>
                                <li>engage in fighting or disorderly conduct, or sexually harass other employees and
                                    community members</li>
                                <li>steal, damage or destroy property belonging to the organisation, its employees or
                                    community members</li>
                                <li>work intoxicated or under the influence of controlled or illegal substances</li>
                                <li>bring controlled or illegal substances to the workplace</li>
                                <li>smoke on the service’s premises or in its motor vehicles</li>
                                <li>smoke with service users.</li>
                            </ul>
                        </li>
                    </ul>
                    <h3><span style="color: #fcb53b;">Dress Code</span></h3>
                    <p>Employees and management should:</p>
                    <ul>
                        <li>dress to comply with workplace health and safety regulations relevant to their work activities
                        </li>
                        <li>dress suitably for their position, presenting a clean, neat and tidy appearance at all times
                        </li>
                        <li>wear a uniform (if supplied) and maintain its condition (clean and not torn)</li>
                        <li>consult with the manager or program coordinator if unsure of the type of clothing appropriate to
                            their position.</li>
                    </ul>
                    <p>Employees who deliberately breach this dress code may receive disciplinary action.</p>
                    <h3><span style="color: #fcb53b;">Dealing with Aggressive Behaviour</span></h3>
                    <p>Employees are expected to provide high standards of service provision. However, the service does not
                        accept any form of aggressive, threatening or abusive behaviour towards its employees by community
                        members or other employees.</p>
                    <p>Suppose an employee is unable to calm the person and/or believes the situation places them or other
                        employees in danger. They should notify the manager or their program coordinator/ supervisor in that
                        case.</p>
                    <p>All incidents must be reported to the service coordinator/supervisor using the Critical Incident
                        Form, and the client must be informed that the behaviour has been reported.</p>
                    <h3><span style="color: #fcb53b;">Use of Computers, Telephones, Facsimiles</span></h3>
                    <p>Unauthorised access and use of confidential information can severely damage the reputation of the
                        service and undermine personal privacy.</p>
                    <p>Employees and management should ensure the following:</p>
                    <ul>
                        <li>Use communication and information devices for officially approved purposes only.</li>
                        <li>Use these communication and information devices for limited personal use as long this use does
                            not interfere with their daily duties.</li>
                        <li>Don’t share their password/s with another employee or share another employee’s password/s.</li>
                    </ul>
                    <h3><span style="color: #fcb53b;">Use of the Internet and Email</span></h3>
                    <p>Internet and email are provided to employees and management for genuine work-related purposes.</p>
                    <ul>
                        <li>Employees and management should:
                            <ul>
                                <li>limit personal use to a minimum; the service may monitor the use and call upon employees
                                    to explain their use</li>
                                <li>comply with copyright regulations when using the internet or email.</li>
                            </ul>
                        </li>
                        <li>Employees and management should not:
                            <ul>
                                <li>divulge personal or confidential information via the internet or email</li>
                                <li>use the internet to access websites or send emails of an explicit sexual nature or in
                                    any manner that breaches the Equity, Anti-discrimination and Workplace Harassment Policy
                                    and Procedure.</li>
                            </ul>
                        </li>
                    </ul>
                    <p>While the privacy of all employees is respected, emails may be used as evidence if legal action is
                        taken against an employee.</p>
                    <p>This information may also be used as evidence of a breach of the Code of Conduct or the Equity,
                        Anti-discrimination and Workplace Harassment Policy and Procedure.</p>
                    <h3><span style="color: #fcb53b;">Treating Others with Respect and Courtesy</span></h3>
                    <p>All persons associated with this service (patients and their families, employees, students and
                        volunteers, management, local residents and businesses, and people from other agencies) have the
                        right to be treated with respect and courtesy.</p>
                    <p>Employees will aim at all times to act fairly, in good faith and without bias or prejudice. Employees
                        shall endeavour to ensure that their own prejudices or biases do not lead to discrimination against
                        any individual or interfere with their capacity to carry out their duties according to their
                        position description and this Code of Conduct. Where bias or prejudice does occur, staff are
                        expected to consult with their personal supervisor or Coordinator to address the issue.</p>
                    <p>Employees will recognise the right of all persons to make their own decisions and to work out their
                        own solutions, having due regard for their personal wellbeing. Support may be offered, and options
                        suggested, but the final decision is the right of each person.</p>
                    <h3><span style="color: #fcb53b;">Confidentiality and Privacy</span></h3>
                    <p>Information about patients, employees, students, volunteers and management will remain private and
                        confidential at all times.</p>
                    <p>Information about any particular patient shall remain confidential within the organisation between
                        workers and shall not be used in any work with that patient without the patient’s explicit
                        permission, except in the following circumstances:</p>
                    <ul>
                        <li>when a patient’s case file has been subpoenaed under law</li>
                        <li>when the consumer has disclosed that they plan to cause harm to themselves</li>
                        <li>when the consumer has disclosed that they plan to cause harm to others</li>
                        <li>when the consumer has disclosed that they are at risk of harm by another.</li>
                    </ul>
                    <p>These exceptions to confidentiality must be explained to all patients who use the service, especially
                        where discussions relate to illegal activity. In these circumstances, a decision to break
                        confidentiality can only be taken after a discussion with the Coordinator.</p>
                    <p>Information obtained in counselling or casework relationships and research data concerning patients
                        may be communicated only for professional purposes and only to persons legitimately concerned with
                        the case plan upon the consent of the patient. A patient must also give consent for staff to access
                        information about them from an external source. The information obtained from a patient and used for
                        the purpose of research or service evaluation shall be coded in such a way that the identity of the
                        person remains anonymous.</p>
                    <p>Securely store personal information provided by a service user or employee.</p>
                    <p>Take reasonable steps to ensure this material is kept secure against:</p>
                    <ul>
                        <li>loss</li>
                        <li>unauthorised access</li>
                        <li>use</li>
                        <li>modification or disclosure</li>
                        <li>misuse</li>
                    </ul>
                    <p>Keep information about all service provisions confidential within the organisation. Do not directly
                        or indirectly disclose information associated with the service to external parties unless authorised
                        by the manager or program supervisor.</p>
                    <h3><span style="color: #fcb53b;">Professional Integrity</span></h3>
                    <p>Employees accept that professional responsibility must take precedence over personal aims, needs and
                        views.</p>
                    <h4><span style="color: #b67318;">Dual relationships</span></h4>
                    <ul>
                        <li>Employees must avoid entering into therapeutic relationships with people they already have a
                            personal relationship with.</li>
                        <li>Employees must avoid entering into personal relationships with people they already have a
                            therapeutic relationship with.</li>
                        <li>Employees are expected to declare any conflict of interest that may impair professional
                            judgment. It does not result in unfair advantages or disadvantages to themselves, clients, other
                            employees, committee members, organisations, or businesses.</li>
                        <li>Suppose a person known personally to an employee becomes a client. In that case, the employee
                            should immediately inform the Coordinator or Chairperson of the duality of roles and discuss
                            strategies for managing the situation.</li>
                        <li>Employees must not have their partners accompany them while undertaking support work. In
                            exceptional circumstances, approval must be obtained from the Coordinator or Chairperson.</li>
                    </ul>
                    <h4><span style="color: #b67318;">Physical contact</span></h4>
                    <p>Physical contact with a patient should be guided by the nature of the professional relationship
                        between the worker and the patient, with due regard to permission and respect for personal space.
                        Consideration should be given to the possible impact of the contact on the patient and how the
                        patient may construe the contact both at the time and in the future.</p>
                    <h4><span style="color: #b67318;">Sexual contact</span></h4>
                    <p>Sexual relationships of any kind between an employee and a patient are unacceptable. This includes
                        activities that are initiated by the patient as well as any activity of a sexual nature engaged in
                        by a patient for the benefit of an employee.</p>
                    <h4><span style="color: #b67318;">Relationships with ex-clients</span></h4>
                    <p>The standards of conduct outlined in this document apply to any contact with a patient who has been a
                        client in the past. Consideration must be given to factors such as:</p>
                    <ul>
                        <li>the type of relationship</li>
                        <li>potential harmful effects on the client and</li>
                        <li>the length of time elapsed between the cessation of the professional relationship and the
                            commencement of any non-professional relationship.</li>
                    </ul>
                    <p>Where there is potential for a relationship with an ex-consumer, this should be discussed with the
                        Coordinator to ensure it is appropriate and in line with organisational policy.</p>
                    <h4><span style="color: #b67318;">Financial payments</span></h4>
                    <p>Employees may not receive private fees, gratuities or other remuneration for professional work with
                        persons who are entitled to the staff member’s services. Employees may neither give nor receive any
                        remuneration for referring young people to other agencies for professional services.</p>
                    <h4><span style="color: #b67318;">Gifts</span></h4>
                    <p>Employees should not give or accept gifts from clients. Employees should inform the Coordinator or
                        manager if they receive a gift. Employees must ensure that clients are aware of this policy to help
                        reduce the possibility of misunderstanding.</p>

