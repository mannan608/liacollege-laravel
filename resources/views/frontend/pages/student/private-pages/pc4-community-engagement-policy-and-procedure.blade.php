                    <h2><span style="color: #da591f;">Community Engagement Procedure</span></h2>
                    <h3><span style="color: #fcb53b;">Management will ensure the following:</span></h3>
                    <ul>
                        <li>Conversations are held with people who may be impacted by decisions or development planning at
                            Banksia Care.</li>
                        <li>Management is proactive in reaching out to government at all levels, who have an influence on
                            aged and disability care operations in the area.</li>
                        <li>Community engagement is planned and validated and remains part of Banksia Cares continuous
                            improvement plan.</li>
                        <li>Attend professional training or leadership workshops related to Community Engagement to ensure
                            staff are aware and informed of community changes and trends.</li>
                        <li>Regularly review community engagement procedures practices, including Community Support and Risk
                            Assessment Plans and Community Support and Inclusion Plans.</li>
                        <li>Investigate incidents to prevent any reoccurrence of any negative community involvement and
                            implement the change required.</li>
                        <li>Provide and maintain safe resources, residential rooms, facilities, and equipment used in
                            community participation events.</li>
                        <li>Provide a reasonable budget to allow community engagement at events, regular external services
                            for residents and referral services.</li>
                    </ul>
                    <h3><span style="color: #fcb53b;">Staff will ensure the following:</span></h3>
                    <ul>
                        <li>Support community engagement by being welcoming, encouraging and respectful of cultural views
                            and practices.</li>
                        <li>Shop locally – source opportunities within the local community to provide residents with
                            extracurricular activities.</li>
                        <li>Be representative of the community and Banksia Care by being polite, respectful and aware.</li>
                        <li>Ensure all activities sourced through the community or community referrals are assessed for any
                            potential risk to residents or staff before engaging.</li>
                        <li>Report any noticeable or perceived risk from any aspect of engaged community activity to
                            management.</li>
                        <li>Understand that participation is not just communication but is about being involved in actions
                            and decisions.</li>
                    </ul>
                    <h3><span style="color: #fcb53b;">In Respect to Clients/Residents:</span></h3>
                    <ul>
                        <li>Independent Living clients will be supported to remain in their homes and as part of their
                            community for as long as possible.</li>
                        <li>Assistance from the community and staff will be given to ensure tasks such as gardening,
                            cleaning and shopping remain part of Independent Living clients’ everyday lives and are
                            achievable. Residents will be given an opportunity to shop, garden, and clean their own room,
                            where able.</li>
                        <li>Independent Living clients and residents will be supported to remain involved in community
                            participation activities example: sporting/social clubs, seniors groups, libraries, church and
                            cultural groups.</li>
                        <li>Families of Independent Living clients and residents will be seen as part of the wider community
                            network related to the client and included in decision-making, activities and lifestyle support
                            where able, willing and requested.</li>
                        <li>External agencies will utilise referrals to ensure all aspects of the client and resident’s
                            health and wellbeing are attended to.</li>
                    </ul>

