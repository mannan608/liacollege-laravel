                    <h2><span style="color: #da591f;">Policy Statement</span></h2>
                    <p>This policy describes Banksia’s translation and interpreter services at Banksia.</p>
                    <p>The purpose of the Accessing an Interpreter or Translator Policy at Banksia Care is to:</p>
                    <ul>
                        <li>describe the scope of translation and interpreter services and the role they play;</li>
                        <li>clarify who is providing the services to our consumers and staff; and</li>
                        <li>provide a procedure to access these services.</li>
                    </ul>
                    <h2><span style="color: #da591f;">Scope</span></h2>
                    <p>This policy applies to all Banksia Care staff working with translation/interpreter services to
                        maintain high quality care. Banksia translates written texts as needed and provides interpreter
                        services for consumers when required.</p>
                    <ul></ul>
                    <h2><span style="color: #da591f;">Background</span></h2>
                    <p>Banksia Care must always meet the Commonwealth’s Aged Care Quality Standards while working in the
                        aged care sector. These standards describe the acceptable standards of care that Aged Care
                        facilities must deliver. These standards are audited by the Aged Care Quality and Safety Commission
                        to ensure that elderly in home or in an aged care home are supported:</p>
                    <p>1. They are treated with dignity, and respect and, as individuals, make informed choices about their
                        care and lifestyle.</p>
                    <p>2. They are a collaborator in their ongoing assessment and services.</p>
                    <p>3. Safe and appropriate personal and clinical care is provided to them.</p>
                    <p>4. Daily living services and support for Aged Care consumers assist health and wellbeing.</p>
                    <p>5. They understand and can rely on the services we provide to be regular and consistent in quality.
                    </p>
                    <p>6. They know they can provide feedback and are encouraged to provide suggestions or make complaints.
                        Further, feedback is acted on, and appropriate and timely action is taken.</p>
                    <p>7. Receive quality care from skilled, capable, and knowledgeable Banksia staff.</p>
                    <p>8. They are confident in the services and care we provide.</p>
                    <p>This policy responds to Aged Care Quality Standards to:</p>
                    <ul>
                        <li>ensure Banksia staff can communicate clearly with our consumers and make informed decisions
                            about the Banksia services consumer’s access</li>
                        <li>ensure Banksia staff can assist our consumers in understanding external communications and
                            making informed decisions</li>
                        <li>deliver reliable quality services at all times</li>
                        <li>always take a collaborative approach in the delivery of our services</li>
                        <li>listen to the people we care for to gather feedback and address complaints.</li>
                    </ul>
                    <h2><span style="color: #da591f;">Legislative Requirements</span></h2>
                    <ul>
                        <li>The Occupational Health and Safety Act 2011 and applicable State/Territory WHS legislation</li>
                        <li>Aged Care Quality and Safety Commission Act 2018 and Aged Care Act 1997</li>
                        <li>Racial Discrimination Act 1975</li>
                    </ul>
                    <h2><span style="color: #da591f;">Principles that Inform our Policy</span></h2>
                    <p>All decision-making about our Accessing an Interpreter or Translator Procedure is carried out in
                        accordance with the principles of our Accessing an Interpreter or Translator Policy. The policy
                        addresses the needs of consumers and carers.</p>
                    <p>Note that Banksia Care staff may be hired who have come from a NESB (Non-English-Speaking
                        Background). To provide safe, quality care, it is a requirement that the English literacy skills of
                        staff are tested and pass IELTS literacy examination.</p>
                    <h3><span style="color: #fcb53b;">In Relation to Consumers</span></h3>
                    <p>The Aged Care Act 1997 states that:</p>
                    <p>“To facilitate access to aged care services by those who need them, regardless of race, culture,
                        language, gender, economic circumstance or geographic location” (Chap 1 Div. 2.1).</p>
                    <p>For Banksia Care to meet this requirement, consideration of NESB (Non-English-Speaking Background)
                        consumers is a priority.</p>
                    <p>At all times – written, aural, visual and any other communication with consumers who natively speak a
                        LOTE (Language Other than English) must be offered translation and interpreter services in their
                        native language. A consumer may express that English is an acceptable language for all or some
                        communications and not require this service.</p>
                    <p>Translation and interpreter services are provided as need assist with:</p>
                    <ul>
                        <li>reading documents</li>
                        <li>writing communications (email/letter/responding to internal Banksia and external correspondence)
                        </li>
                        <li>conversation (talking to Banksia staff and external parties).</li>
                    </ul>
                    <h3><span style="color: #fcb53b;">In Relation to Carers</span></h3>
                    <p>Some consumers will engage through carers with Banksia Care staff.</p>
                    <p>Where carers translate or interpret on behalf of consumers:</p>
                    <ul>
                        <li>Any Banksia service delivery requirement that requires a consumer’s signature must be provided
                            in a document in the native language of the consumer. Additionally, conversations between
                            Banksia Care and consumers must be interpreted by a Banksia Care approved service as needed.
                        </li>
                        <li>If a carer acts in a legal capacity on behalf of a consumer, due to a consumer being
                            incapacitated, and carer requires translation or interpretation services, these will be provided
                            by Banksia Care.</li>
                    </ul>
                    <h3><span style="color: #fcb53b;">Ensuring understanding and collaboration</span></h3>
                    <p>All Banksia Care staff are to ensure that consumers are informed and collaborated on any matter
                        regarding Banksia Care’s service delivery before any change in service is implemented. Similarly,
                        where carers act on behalf of consumers, they must be fully informed and collaborated with before
                        any change is implemented. If a Banksia Care staff member believes consumers or carers are either
                        unaware or misunderstand an intended change in service, Banksia Care staff are to refer the issue to
                        their management BEFORE any change in service is agreed to by the consumer or carer.</p>
