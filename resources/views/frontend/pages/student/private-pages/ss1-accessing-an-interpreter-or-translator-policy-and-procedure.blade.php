                    <h2><span style="color: #da591f;">Accessing an Interpreter or Translator Procedure</span></h2>
                    <ul>
                        <li>Banksia Care relies on the Australian Government Language Services Guidelines to provide
                            direction on the provision of interpretation and translation services. Interpretation deals with
                            spoken language in real-time, while translation focuses on written content.</li>
                        <li>Banksia relies on language services from suppliers registered with the National Accreditation
                            Authority for Translators and Interpreters (NAATI) or the Australian Institute of Interpreters
                            and Translators (AUSIT). Interpreter services may be requested by Banksia Care staff or by
                            consumers/carers for assistance regarding Banksia Care service delivery.</li>
                        <li>Translation services may be requested by Bankia Care staff as needed for written documents.</li>
                        <li>Consumers/carers may use Banksia Care’s translation and interpreters’ services for non-Banksia
                            Care related business as a fee for service which is billed monthly.</li>
                    </ul>
                    <h3><span style="color: #fcb53b;">Translation</span></h3>
                    <p>When booking translation services, Banksia Care staff are to allow one working day to gather approval
                        from Banksia Care management.</p>
                    <p>Once a booking is approved, staff must provide the following to the chosen translation service:</p>
                    <ul>
                        <li>the information requiring translation</li>
                        <li>the language translation required (such as from English to Hindi or from Greek to English)</li>
                        <li>the format of the translation they are to provide (e.g., Word document)</li>
                        <li>when the translation is required.</li>
                    </ul>
                    <p>Banksia Care expects all translations to be completed within 72 hours of engaging a service.</p>
                    <p>Preferred Translation Services (Monday to Friday, 9 a.m. to 5 p.m.)</p>
                    <p>Babel ready –&nbsp;&nbsp;&nbsp; Phone 1300 000 432</p>
                    <p>Any Words? – &nbsp; &nbsp;Phone 1300 600 987</p>
                    <p>Any documents are considered private and not to be shared in any form with other parties when Banksia
                        staff are assisting in translation.</p>
                    <h3><span style="color: #fcb53b;">Interpreting</span></h3>
                    <p>When booking Interpreting services, Banksia Care staff are to allow one working day to gather
                        approval from Banksia Care management.</p>
                    <p>Once a booking is approved, staff must provide the following to the chosen interpreting service:</p>
                    <ul>
                        <li>the consumer’s name</li>
                        <li>the language/dialect required</li>
                        <li>the preferred gender of the interpreter</li>
                        <li>the date and time the interpreter is required (include time to brief the interpreter)</li>
                        <li>the type of appointment (e.g., medical, legal, individual or group session)</li>
                        <li>Banksia’s service number for billing</li>
                        <li>the approximate duration of the appointment</li>
                        <li>whether you or the client wish to have a specific interpreter for continuity of care reasons
                        </li>
                        <li>the telephone system (fixed, mobile, speaker) being used.</li>
                    </ul>
                    <p>If a Consumer or Carer refuses to use a translation service and, in the opinion of Banksia Care
                        staff, one is required, then:</p>
                    <ul>
                        <li>explain to the person that it is the staff member who requires the language service, not them
                        </li>
                        <li>speak with family members or friends (carers) to establish the reason why the person is refusing
                            the language service</li>
                        <li>engage a telephone interpreter to assist in establishing the issues and explain the need for the
                            language service.</li>
                    </ul>
                    <p>If an interpreter is needed and refused, Banksia Care staff are to defer and not force
                        consumers/carers to use an interpreter.</p>
                    <p>Preferred Interpreter Services (These are 24-hour services)</p>
                    <p>Babel ready –&nbsp;&nbsp;&nbsp; Phone 1300 000 432</p>
                    <p>All voice – &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Phone 13 19 00</p>
                    <h3><span style="color: #fcb53b;">Emergency Interpreter</span></h3>
                    <p>If an emergency requires an interpreter:</p>
                    <ul>
                        <li>Do not wait for an interpreter before calling emergency services. Always call an emergency
                            service as a first priority.</li>
                        <li>When the situation allows, refer to the Banksia Care staff language registry to establish if
                            there is a staff member on-site with the appropriate language skills who can assist.</li>
                        <li>Call a preferred interpreter service and ensure that they understand that you require immediate
                            emergency assistance. Ensure the service understands the nature of the emergency.</li>
                    </ul>

