                    <h2><span style="color: #da591f;">Palliative Care Procedure</span></h2>
                    <ul></ul>
                    <h3><span style="color: #fcb53b;">Training for Staff</span></h3>
                    <p>Banksia staff hold a minimum level of core competencies to provide care for individuals at the end of
                        life.</p>
                    <p>In accordance with the Royal Commission into Aged Care and Quality and Safety, this includes, at a
                        minimum:</p>
                    <ul>
                        <li>understanding variation in illness paths;</li>
                        <li>management of bodily symptoms;</li>
                        <li>identifying suffering due to unmet palliative care needs;</li>
                        <li>management of depression and anxiety;</li>
                        <li>awareness of cultural and spiritual needs;</li>
                        <li>recognising decline and dying;</li>
                        <li>an understanding of advance care planning and goals of care;</li>
                        <li>delivering care suitable to a care recipients care management plan;</li>
                        <li>delivering reliable patient focussed care;</li>
                        <li>awareness of legal responsibilities;</li>
                        <li>effective interaction with people living with a terminal illness and their carers and families;
                        </li>
                        <li>an understanding of grief and bereavement; and</li>
                        <li>self-care.</li>
                    </ul>
                    <ul></ul>
                    <h3><span style="color: #fcb53b;">Care Planning</span></h3>
                    <ul>
                        <li>The care plan is based upon an ongoing assessment, goals set with patient and family.</li>
                        <li>The care plan is developed with feedback from patient, family, caregivers, involved health care
                            providers and the palliative care team with the additional input, when indicated, of other
                            specialists and caregivers (e.g., clergy, friends).</li>
                        <li>The care plan process includes formal assessment and documentation to include:
                            <ul>
                                <li>physical and psychological assessment, which addresses the current disease status,
                                    treatment options, functional status, expected prognosis, symptom burden and
                                    psychological coping</li>
                                <li>social and spiritual assessment, which addresses the social, practical, religious,
                                    spiritual, existential concerns, and legal needs of the patient and caregivers,
                                    including but not limited to: relationships, communication, existing social and cultural
                                    networks, decision making, work and school settings, finances, sexuality/intimacy,
                                    caregiver availability and stress, access to medicines and equipment</li>
                                <li>cultural assessment, including, but not limited to, locus of decision making,
                                    preferences regarding disclosure of information, truth telling and decision making,
                                    dietary preferences, language, family communication, desire for complementary and
                                    alternative medicine, perspectives on death, suffering and grieving, and funeral/burial
                                    rituals.</li>
                            </ul>
                        </li>
                        <li>Care planning meetings with a patient and family will occur regularly to determine the most
                            appropriate goals of care as indicated by the clinical conditions and are coordinated by the
                            palliative care team in conjunction with the attending physician and other hospital staff.</li>
                        <li>Care plan changes are based on the evolving needs and preferences of the patient and family over
                            time, recognising the complex, competing and shifting priorities in goals of care.</li>
                        <li>The palliative care team provides support for decision-making, develops and carries out the care
                            plan and communicates the plan to patient, family, involved health professionals and the
                            providers involved with patient transfer to different internal or external care settings.</li>
                    </ul>
                    <ul></ul>
                    <h3><span style="color: #fcb53b;">Safe Handling of Medication</span></h3>
                    <p>Medicine prescription, management, storage, transport, administration and disposal is conducted in
                        line with the Guidelines for handling medicines in community services. Processes meet the terms of
                        state and local policy regulations and workplace policies and procedures.</p>
                    <ul></ul>
                    <h3><span style="color: #fcb53b;">End-of-Life Care</span></h3>
                    <p>Procedure</p>
                    <ol>
                        <li>Meeting patient and family needs is the central focus of care when cure or maintaining the
                            continuum of health is no longer possible. Optimal care requires exceptional communication among
                            clinical staff and a recognition that a team approach, inclusive of many health care
                            disciplines, is necessary.</li>
                        <li>Patients are treated with respect to their individual wishes for care and treatment with
                            consideration of their values, religion and philosophy. A request to discontinue treatment will
                            be honored with the same support and respect as the decision to continue treatment.</li>
                        <li>The palliative care consultation service is available to assist with symptom management,
                            prognosis determination, patient and family support, disposition planning and other issues
                            related to end-of-life decisions.</li>
                        <li>Hospice services are recognised as an integral part of the continuum of care. Patients who have
                            an expected prognosis of six months or less, assuming the disease follows its usual course,
                            should be offered hospice services.</li>
                        <li>Bereavement support and chaplaincy services are available to patients and their family members.
                            Nursing, social services and the chaplaincy program are available to provide resources to
                            families. The bereavement services coordinator can assist with the care team and with aftercare.
                        </li>
                        <li>In the event of questions or differences of opinion among the patient, family or health care
                            team members about the treatment goals, consultation is available from the hospital ethics
                            committee.</li>
                    </ol>
                    <ul></ul>
                    <h3><span style="color: #fcb53b;">Palliative Care: Patient/Family/Caregiver Education</span></h3>
                    <ul>
                        <li>During the assessment process, patient/family/caregiver educational needs and
                            cognition/emotional abilities are assessed and documented. The following is a list of common
                            issues to be addressed, depending on the patient’s unique clinical circumstances:
                            <ul>
                                <li>pain and symptom management, including side-effect management</li>
                                <li>advance care planning and advance directives</li>
                                <li>anticipated future medical needs home or institutional support options (e.g., home
                                    hospice services)</li>
                                <li>what to expect in the normal course of the disease</li>
                                <li>signs/symptoms of approaching death community services (e.g., bereavement, counselling),
                                    whom to call for routine and emergency needs.</li>
                            </ul>
                        </li>
                        <li>Educational/counselling needs are routinely assessed and reassessed throughout care and
                            treatment.</li>
                        <li>When educational needs are identified, they are incorporated into the plan of care.</li>
                        <li>Age, language, and educationally appropriate educational materials (written, Internet, oral)
                            will be provided to meet the needs identified in the assessment process.</li>
                    </ul>

