                    <h2><span style="color: #da591f;">Principles that Inform our Policy</span></h2>
                    <p>All decision making about palliative care is carried out in accordance with the principles of our
                        <em>Palliative Care Management Policy:</em></p>
                    <ul>
                        <li>Special consideration is given to at risk and vulnerable people and those from diverse
                            communities, or Aboriginal and Torres Strait Islander peoples.</li>
                        <li>Individuals are supported to be cared for and die in their chosen environment.</li>
                        <li>Recognise the importance of carers, family and friends in providing physical, emotional, social
                            and spiritual support and care of the individual at the end of life.</li>
                        <li>Utilise family carers to formulate the level of care they would like to provide in essential
                            palliative care.</li>
                        <li>The process of introducing the use of intravenous medicines to help manage breakthrough symptoms
                            is discussed with the person (if appropriate) and the carer/family in the context of death and
                            dying, respecting the person’s specific spiritual, religious and cultural needs.</li>
                        <li>Bereavement support is provided to carers/families should they be participating in breakthrough
                            symptom management.</li>
                    </ul>
