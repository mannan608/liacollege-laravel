                    <h2><span style="color: #da591f;">Principles that Inform our Policy</span></h2>
                    <p>All decision-making about our Management of Care and Services Procedure is carried out in accordance
                        with the principles of our Management of Care and Services Policy.</p>
                    <ul>
                        <li>It is recognised that every member of our team impacts the health and wellbeing of others.</li>
                        <li>We all have a role to play in creating an environment that supports and promotes this within the
                            care community as a whole and as individuals.</li>
                        <li>Staff who are safe, well and working in supportive environments are able to provide the best
                            attainable care to recipients.</li>
                        <li>Our commitment to governance and leadership is enabled through our service philosophy, policies
                            and procedures.</li>
                    </ul>
                    <p>General Considerations</p>
                    <ul>
                        <li>By developing and upholding quality practices for all key staff, we strive to ensure the safety
                            and wellbeing of all stakeholders at all times.</li>
                        <li>All staff (including volunteers) are guided by professional standards at the highest level.</li>
                        <li>Position descriptions serve to guide all staff in their requirement to adhere to all relevant
                            standards, including the code of conduct and code of ethics. In addition, we provide an
                            opportunity for reflection and regular professional development training across all areas.</li>
                        <li>We appoint our responsible person in alignment with regulatory requirements and ensure the
                            service is well managed with quality supervision.</li>
                        <li>Volunteers and care recipients are valued and encouraged. All training is provided by
                            experienced staff with reference to all policies, procedures and industry expectations.</li>
                        <li>The responsibility for staff wellbeing is shared between the care community and individual
                            staff.</li>
                        <li>Everyone in the workplace has a responsibility to promote and maintain a healthy workplace.</li>
                        <li>The responsibility for staff wellbeing also rests with leadership.</li>
                        <li>Leaders will act to improve the mental health of all staff by looking at the cultural,
                            structural and environmental factors within the care community that might support or hinder
                            staff wellbeing.</li>
                        <li>Confidentiality in the management of any employee matter must be maintained.</li>
                    </ul>
                    <p>Considerations for Safety and Hygiene</p>
                    <ul>
                        <li>Provide a smoke-free work environment and encourage and support staff to quit smoking.</li>
                        <li>Staff may be experiencing domestic and family violence, and its aftermath may impact their
                            attendance at work or their ability to perform work duties.</li>
                        <li>The pandemic can affect the mental health and wellbeing of staff, so strict adherence to COVID
                            safe practices is critical.</li>
                        <li>Provide healthy eating choices in the workplace by addressing healthy physical settings, such as
                            food storage and preparation areas.</li>
                        <li>Provide an opportunity for engaging socially with peers through the provision of staff rooms,
                            break-out areas or outdoor seating areas.</li>
                        <li>Provide access to information and resources that increase worker knowledge and awareness around
                            key health areas.</li>
                        <li>Banksia Care will ensure they are meeting its Workplace Health and Safety (WHS) requirements to
                            avoid injury or illness.</li>
                        <li>Information on various support services will always be readily available such as the Black Dog
                            Institute, Beyond Blue and Lifeline.</li>
                    </ul>
