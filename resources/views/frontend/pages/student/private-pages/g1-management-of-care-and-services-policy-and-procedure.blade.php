                    <h2><span style="color: #da591f;">Staff Management and Professional Development Procedure</span></h2>
                    <h3><span style="color: #fcb53b;">Management will ensure the following:</span></h3>
                    <ul>
                        <li>Appoint a nominated supervisor for periods when the service or the approved provider is absent.
                        </li>
                        <li>Ensure all staff in charge are fully aware of obligations related to guardianship and human
                            rights laws.</li>
                        <li>Ensure the nominated supervisor has displayed the name of the nominated supervisor at the main
                            entrance.</li>
                        <li>In writing, advise regulatory authorities of any changes to the nominated supervisor.</li>
                        <li>Detail the name of the nominated supervisor on all records for each occasion care recipients
                            attend the care service.</li>
                        <li>Ensure that no staff member, volunteer or practicum placement student is affected by drugs or
                            alcohol whilst attending the service.</li>
                        <li>Provide an opportunity for regular professional development, including training on the Charter
                            of Aged Care Rights and National Quality Standards.</li>
                    </ul>
                    <h3><span style="color: #fcb53b;">The nominated supervisor/responsible person will ensure the
                            following:</span></h3>
                    <ul>
                        <li>Ensure that they meet their obligations in relation to the regulations affecting staffing
                            arrangements.</li>
                        <li>Ensure they are present whenever the approved provider is unavailable or absent.</li>
                        <li>Never be affected by drugs or alcohol whilst at the service, and ensure the same applies to
                            their staff, practicum placement students and volunteers.</li>
                        <li>Be fully aware of obligations related to the Charter of Aged Care Rights and National Quality
                            Standards laws and complete any jurisdictional requirements for training.</li>
                        <li>Undertake any professional development training as directed by management to ensure currency of
                            knowledge.</li>
                        <li>Display at the main entrance the name of the nominated supervisor.</li>
                        <li>In the event the name or details of the nominated supervisor change, advise the approved
                            provider immediately.</li>
                        <li>Meet all responsibilities in relation to the delivery of all service programs, including but not
                            limited to supervision, welfare intent, safety, health, wellbeing, medication, collection and
                            delivery to service, ratios of staff, and professional development opportunities.</li>
                        <li>Record the name of the nominated supervisor for every session care recipients are cared for at
                            the care service.</li>
                    </ul>
                    <h3><span style="color: #fcb53b;">Aged care workers/staff will ensure the following:</span></h3>
                    <ul>
                        <li>Be aware of and follow the Management of Care and Services Policy and Procedure.</li>
                        <li>Ensure the State requirements concerning education and care qualifications, first aid
                            qualifications, immunisation history statements and police clearances, or the Australian College
                            of Care Workers (ACCW) registration details (jurisdiction dependant) are met and always
                            maintained.</li>
                        <li>Attend any professional development identified as required by management.</li>
                        <li>Not consume or be intoxicated by alcohol or drugs while employed or engaged by the service and
                            in control or supervision of children.</li>
                        <li>Abide by the Code of Ethics.</li>
                        <li>Adhere to the Code of Conduct.</li>
                        <li>Ensure quality staffing practices are implemented in line with the National Quality Standard
                            (especially Quality Standard 7 – Human Resources).</li>
                    </ul>

