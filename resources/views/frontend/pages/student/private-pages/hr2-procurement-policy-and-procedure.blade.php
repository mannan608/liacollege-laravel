                    <h2><span style="color: #da591f;">Procurement Procedure</span></h2>
                    <ul></ul>
                    <h3><span style="color: #fcb53b;">Management will ensure the following:</span></h3>
                    <ul>
                        <li>The premises and facilities are designed and maintained to always facilitate the supervision of
                            patients and residents to ensure their safety by utilising Banksia Care’s budget allocation for
                            such purposes.</li>
                        <li>Staff are aware of the procurement policy.</li>
                        <li>Facilities or services contracted by the organisation have first been assessed to ensure the
                            safety of patients and residents people.</li>
                    </ul>
                    <h3><span style="color: #fcb53b;">Residential Manager will ensure the following:</span></h3>
                    <ul>
                        <li>Collaborate with the Board to develop an annual spend allocation for the Banksia Care service
                            based on the budget allocation given.</li>
                        <li>Participate in reviewing the effectiveness of the Banksia Care budget.</li>
                        <li>Be responsible for ensuring that funds are spent as budgeted and agreed on.</li>
                        <li>Ensure all expenditure is for valid Banksia Care expenses by way of them being authorised,
                            appropriate, required and received in good order and as anticipated.</li>
                        <li>Comply with the procurement policy.</li>
                        <li>Ensure that a Request for Quote Form has been provided and assessed before an authorisation is
                            given for any purchase over $200.</li>
                        <li>Ensure a Procurement Management Plan is completed for any purchase over $200.</li>
                        <li>Manage annual budget using Banksia Care records/systems provided.</li>
                    </ul>
                    <h3><span style="color: #fcb53b;">For Purchases Less Than $200</span></h3>
                    <ul>
                        <li>No quote from suppliers is required.</li>
                        <li>No risk identification is required.</li>
                        <li>No contract is required.</li>
                        <li>Receipts must be obtained and provided to management.</li>
                    </ul>
                    <h3><span style="color: #fcb53b;">For Purchases Greater Than $200</span></h3>
                    <ul>
                        <li>A minimum of three quotes must be sought from suppliers, with one quote being sought from a
                            local supplier (if possible) using the Request for Quote Template.</li>
                        <li>Record expenditure in the Deviation Analysis.</li>
                        <li>Complete a Procurement Management Plan.</li>
                    </ul>
                    <h3><span style="color: #fcb53b;">Supplier selection should be based on the following selection
                            criteria:</span></h3>
                    <ul>
                        <li>Quality (technical capability)</li>
                        <li>Sustainability</li>
                        <li>Value for money/price.</li>
                    </ul>
                    <h3><span style="color: #fcb53b;">Banksia Care staff will ensure the following:</span></h3>
                    <ul>
                        <li>Seek approval from the Residential Manager prior to any purchase.</li>
                        <li>Provide receipts immediately after purchase to the Residential Manager.</li>
                        <li>Agree that where own funds have purchased an item (for example, milk) – no reimbursement will be
                            given if a receipt is not provided.</li>
                        <li>Provide ideas/suggestions to the Residential Manager for consideration of any resource in the
                            Banksia Care budget.</li>
                    </ul>

