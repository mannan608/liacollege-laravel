                    <h2><span style="color: #da591f;">Policy Statement</span></h2>
                    <p>The purpose of the Procurement Policy at Banksia Care is to provide mandatory requirements for
                        Banksia Care staff (particularly the Coordinator) concerning the purchase of goods and services.</p>
                    <ul></ul>
                    <h2><span style="color: #da591f;">Scope</span></h2>
                    <p>This policy applies to auxiliary staff, management and educators at Banksia Care.</p>
                    <ul></ul>
                    <h2><span style="color: #da591f;">Background</span></h2>
                    <p>Staff of Banksia are provided with a budget following approval from the Board at the AGM. Dr.
                        Him-Chan Lee (CEO) will discuss the annual budget with the Residential Manager and Care Coordinator.
                    </p>
                    <p>With support from Dr. Him-Chan Lee, a procurement plan will be created for proposed expenditure
                        across resources, consumables and professional development. Maintenance and grounds will be the
                        responsibility of the Facility and will be allocated in the organisation’s budget.</p>
                    <p>The Residential Manager must keep a deviation analysis to track spending vs budget areas. All
                        procurement with a value greater than $200 (not including groceries) can only be purchased following
                        a Request for Quote process and completion of a procurement management plan.</p>
