                    <h2><span style="color: #da591f;">NDIS Support Coordination Procedure</span></h2>
                    <ul></ul>
                    <h3><span style="color: #fcb53b;">The Allocated NDIS Support Coordinator:</span></h3>
                    <ul>
                        <li>Receives request for service via secure encrypted email.</li>
                        <li>Contacts NDIS participant as soon as possible (ideally within two days but recognise this is not
                            always practical).</li>
                        <li>Meets with the participant (ideally within five days but recognise this is not always
                            practical).</li>
                        <li>Explains the support coordination role.</li>
                        <li>Discusses the service agreement with the participant with regard to support coordination role.
                        </li>
                        <li>Explains conflict of interest in terms of support coordination role.</li>
                        <li>Explains the NDIS system and their plan, including the budgeted funding within the plan.</li>
                        <li>Assists participant to create a mygov account (if required) and log into the portal.</li>
                        <li>Creates service booking for support coordination.</li>
                        <li>Works with participant to identify what supports need to be implemented as a priority.</li>
                        <li>Communicates with other required supports to commence provision of service/support.</li>
                        <li>Follows up on service agreements and timeframes of support provision.</li>
                        <li>Completes Participant case notes, updating at each point of contact and activity.</li>
                        <li>Completes Support Log hours for each activity.</li>
                    </ul>
                    <h3><span style="color: #fcb53b;">All NDIS Support Coordinators are expected to:</span></h3>
                    <ul>
                        <li>Accept referrals and engage NDIS participants to complete service agreements.</li>
                        <li>Support participants to understand their NDIS plan and the funding allocations and supports
                            outlined.</li>
                        <li>Engage with participants and collaboratively implement the funded plan.</li>
                        <li>Provide assistance linking participants to appropriate providers to meet the needs identified in
                            their individually funded plans.</li>
                        <li>Coordinate a range of supports including formal, mainstream and funded providers and support
                            participant to complete service agreements where required.</li>
                        <li>Engage in formal and formal support networks.</li>
                        <li>Resolve points of conflict or crisis to develop customers capacity and resilience within their
                            own network, and community.</li>
                        <li>Develop intervention plans for implementation by support workers as required.</li>
                        <li>Advocate for the customer as needed or requested.</li>
                        <li>Maintain up to date participant records/case notes.</li>
                        <li>Provide regular feedback and reports to the NDIS and participate in the review of the plan as
                            required.</li>
                    </ul>
                    <h3><span style="color: #fcb53b;">Rights and responsibilities of Managers:</span></h3>
                    <p>Prior to employment or engagement, Management will ensure that all Support Coordination employees or
                        contractors have a:</p>
                    <ul>
                        <li>National Police Check</li>
                        <li>Working With Children Check (if working with children)</li>
                        <li>Worker Screening Check to ensure the safety and wellbeing of all participants.</li>
                    </ul>
                    <p>Management have the right to refuse employment or engagement if these requirements are not met in
                        full.</p>
                    <p>Management must examine participant complaints within ten business days, providing a response within
                        15 business days, in line with the Banksia Care Customer Complaint Policy and procedure.</p>
