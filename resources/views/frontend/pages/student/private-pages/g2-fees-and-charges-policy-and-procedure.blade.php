                    <h2><span style="color: #da591f;">Fees and Charges Procedure</span></h2>
                    <p>Banksia Care will use the following procedures to ensure residents, clients and families are informed
                        about the fees of enlisting our service and all conditions that apply. They will also be used to
                        encourage residents, clients and families to prepare and plan for their fees to be paid correctly
                        and on time.</p>
                    <h3><span style="color: #fcb53b;">Management will Ensure the Following:</span></h3>
                    <ul>
                        <li>Be transparent and discuss fees prior to the resident/client commencing with the service.</li>
                        <li>Accept placement or provide a service on the understanding that full disclosure of all
                            individual needs that may require additional services or resources have been disclosed.</li>
                        <li>Provide written information, including this policy regarding prices, fees, payments, and all
                            conditions.</li>
                        <li>Reserve the right to change fees at any time but will ensure written notice of six (6) months is
                            given to the resident, client or family.</li>
                        <li>Provide a receipt and proof of payments made.</li>
                        <li>Provide payment plans on the approved application.</li>
                        <li>Be responsible for the billing and collection of fees.</li>
                        <li>Enact a debt collector if needed following 21 days of unpaid accounts with no payment plan.</li>
                        <li>Approve an extension of time for payment in exceptional circumstances that are out of the
                            resident’s/client’s/family’s control.</li>
                    </ul>
                    <h3><span style="color: #fcb53b;">Residents/Clients/ Families will Ensure the Following:</span></h3>
                    <ul>
                        <li>Agree that on signing the contract with Banksia Care, they are jointly and severally liable for
                            the fees and any other associated costs unless funding arrangements are in place.</li>
                        <li>Give a minimum of one quarter’s notice, in writing, to Banksia Care, prior to the withdrawal
                            date should they wish to cancel the service provided by Banksia Care.</li>
                        <li>Be charged for the subsequent quarter’s fees where the above notice is not given.</li>
                        <li>Agree that American Express, Visa or MasterCard payments will attract a surcharge. The
                            percentage surcharge applied will not be in excess of the costs incurred by Banksia Care for
                            receiving credit card payments.</li>
                        <li>Understand who is responsible for fees – all or part. The Australian Government, NDIA, DVA or
                            themselves or a combination of such.</li>
                    </ul>
                    <h3><span style="color: #fcb53b;">In relation to Direct Debit Payments:</span></h3>
                    <ul>
                        <li>A completed direct debit form must be received before commencement.</li>
                        <li>Residents/clients/families will be issued a fee statement via email on a per-quarter basis.</li>
                        <li>A dishonour fee will apply for direct debit transactions with insufficient funds to cover the
                            fees.</li>
                    </ul>
                    <h3><span style="color: #fcb53b;">In relation to Financial Difficulty:</span></h3>
                    <ul>
                        <li>If a resident/client/family experiences financial challenges, Banksia Care may agree to develop
                            a payment plan that will be decided through collaboration with management.</li>
