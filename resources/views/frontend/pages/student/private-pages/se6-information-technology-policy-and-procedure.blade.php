                    <h2><span style="color: #da591f;">Information Technology Procedure</span></h2>
                    <h3><span style="color: #fcb53b;">Computer Logon</span></h3>
                    <ol>
                        <li>Banksia Care provides all staff members with a username and password to access the computer
                            system and services provided at Banksia Care.</li>
                        <li>All staff must ensure they keep their login details secure and protected.</li>
                        <li>This login will provide access to email, the Internet and the organisation’s cloud-based file
                            storage and services.</li>
                        <li>All files must be accessed and saved according to Document Management Policy and Procedures.
                        </li>
                    </ol>
                    <h3><span style="color: #fcb53b;">Acceptable Use of Systems</span></h3>
                    <ol>
                        <li>All electronic information and technological resources provided by Banksia Care are for business
                            use only. They are only to be used for the purpose of performing authorised lawful business
                            activities. The downloading, viewing, distribution and/or copying of non-business material,
                            including but in no way limited to pornographic, offensive, or discriminative material is not
                            permitted.</li>
                        <li>Should staff make incidental use of the email system to transmit personal messages. Such
                            messages shall be treated no differently from other messages. Banksia Care reserves the right to
                            access, copy or delete all such messages for any purpose and to disclose them to any party
                            deemed appropriate by the system owner.</li>
                        <li>Personal use of email facilities must not breach the following:
                            <ul>
                                <li>Interfere with normal business activities.</li>
                                <li>Involve any form of solicitation.</li>
                                <li>Be associated with any for profit outside of business activity.</li>
                                <li>Potentially embarrass or offend Banksia Care or any staff member or client of Banksia
                                    Care.</li>
                                <li>Break privacy, copyright, or intellectual property laws.</li>
                            </ul>
                        </li>
                    </ol>
                    <h3><span style="color: #fcb53b;">Non-acceptable Use of Systems</span></h3>
                    <ol>
                        <li>Banksia Care will not tolerate the use of any of its electronic, information and technological
                            resources for the sending, receiving or forwarding of emails or communication which is:</li>
                    </ol>
                    <ul>
                        <li>defamatory in content
                            <ul>
                                <li>discriminatory, racist or sexist</li>
                                <li>abusive, obscene or where language content could be considered offensive</li>
                                <li>sexually harassing</li>
                                <li>pornographic</li>
                                <li>junk mail, such as chain letters and non-business graphics, audio and sound</li>
                                <li>internal appeal.</li>
                            </ul>
                        </li>
                    </ul>
                    <ol start="2">
                        <li>The opening of email attachments from untrusted or unknown sources is not permitted.</li>
                    </ol>
                    <h3><span style="color: #fcb53b;">Emails</span></h3>
                    <ol>
                        <li>Emails sent externally or internally are sent by an individual representing the company and
                            should be treated in the same way as written correspondence. All outgoing emails must include
                            salutations and a staff member’s Banksia Care signature including disclaimer.</li>
                        <li>Disclaimer wording to be included to emails: This email and any files transmitted with it are
                            confidential and intended solely for the use of the individual or entity to which they are
                            addressed. Please notify the sender if you receive this email in error. Please note that any
                            views or opinions presented in this email are solely those of the author and are not necessarily
                            those of Banksia Care. We accept no liability for any damage caused by any virus transmitted by
                            this email and advise the recipient check this email and any attachments for the presence of
                            such.</li>
                    </ol>
                    <h3><span style="color: #fcb53b;">Monitoring</span></h3>
                    <ol>
                        <li>All messages and associated file attachments sent by employees of Banksia Care are the property
                            of Banksia Care.</li>
                        <li>Banksia Care has the right to read, monitor, track, record, copy or delete the contents of a
                            staff member’s mailbox at its discretion.</li>
                        <li>Banksia Care has the right to monitor, track, or record an individual’s use of the Internet.
                        </li>
                    </ol>
                    <h3><span style="color: #fcb53b;">Liability</span></h3>
                    <ol>
                        <li>Comments that are not appropriate in the workplace are not appropriate on the email network.
                        </li>
                        <li>Banksia Care understands that it may be liable for what its staff write in email messages. The
                            audience of an email message may be unexpected and widespread. Staff must not assume that email
                            messages are private or secret. Email messages can be easily copied, forwarded, saved,
                            intercepted, and archived and could be the subject of discovery if Banksia Care is involved in
                            litigation.</li>
                        <li>All information stored in email accounts is ‘documented’ and therefore discoverable in
                            litigation. It is, therefore, the responsibility of every employee to refrain from using
                            unnecessary or inappropriate messages in email.</li>
                    </ol>
                    <h3><span style="color: #fcb53b;">Confidential Information</span></h3>
                    <p>The transmission of commercially confidential information to competitors, affiliates, other
                        organisations and external entities or persons of Banksia Care and its clients is not permitted.</p>
                    <ul></ul>
                    <h3><span style="color: #fcb53b;">Storage of Information</span></h3>
                    <ol>
                        <li>All staff are required to store files as they are created and/or adjusted on the cloud-based
                            file storage system in the folder relevant to the related quality area.</li>
                        <li>All electronic folders include an ‘Archived’ folder where non-current versions are to be stored
                            as a new one becomes available.</li>
                        <li>Staff are expected to ensure that the cloud-based storage system is kept up-to-date and accurate
                            at all times and always ensure the correct use of the Document Management Policy and Procedures
                            policy.</li>
                    </ol>
                    <h3><span style="color: #fcb53b;">Compliance with Policy</span></h3>
                    <ol>
                        <li>If any person at Banksia Care becomes aware of the misuse of email or the Internet, that person
                            should immediately report it to Senior Management for investigation.</li>
                        <li>If any person is offended, humiliated, intimidated or embarrassed by the use of an email or the
                            Internet by other employees, that person should also report it to Senior Management.</li>
                        <li>Banksia Care is committed to the terms of this policy and will thoroughly investigate and deal
                            with all incidents of breach of this policy.</li>
                    </ol>
