                    <h2><span style="color: #da591f;">Emergency Management &nbsp;Procedure</span></h2>
                    <h3><span style="color: #fcb53b;">Management will</span></h3>
                    <ul>
                        <li>comply with the emergency response procedure</li>
                        <li>ensure appropriate debriefing and counselling is supplied to staff, residents, visitors,
                            volunteers and medical professionals after an emergency response.</li>
                    </ul>
                    <h3><span style="color: #fcb53b;">Develop and display of emergency procedures</span></h3>
                    <ul>
                        <li>Develop emergency response procedures suitable for their premises.</li>
                        <li>Ensure staff are trained and familiar with emergency management procedures.</li>
                        <li>Ensure evacuation plans are displayed visible in each room and at each exit of the service.</li>
                        <li>Display emergency telephone numbers near phones and in rooms.</li>
                        <li>Set out clear instructions of what must be done in an emergency response.</li>
                    </ul>
                    <h3><span style="color: #fcb53b;">Provide and manage emergency equipment</span></h3>
                    <ul>
                        <li>Provide a working alarm to notify staff of emergency.</li>
                        <li>Ensure the service has adequately placed fire extinguishers that are tagged and checked every
                            six months.</li>
                        <li>Provide fire safety blanket in the kitchen with appropriate signs identifying the location.</li>
                        <li>Provide emergency bags for all community rooms these should include – first aid kits, contact
                            details of residents, phone, and water.</li>
                        <li>Ensure all doors and windows are fitted with working locks and blinds can be easily shut down if
                            required.</li>
                    </ul>
                    <h3><span style="color: #fcb53b;">Compliance and documentation of emergency management</span></h3>
                    <ul>
                        <li>Conduct risk assessment for both evacuation and lockdown.</li>
                        <li>Complete and document a thorough review and assessment of all emergency drills on the emergency
                            response register.</li>
                        <li>Communicate to staff the result of practice drills and changes to practices made.</li>
                        <li>Ensure all documentation and registers are filed appropriately in office.</li>
                    </ul>
                    <h3><span style="color: #fcb53b;">Emergency response rehearsals</span></h3>
                    <ul>
                        <li>Ensure rehearsals are compliant with regulations.</li>
                        <li>Ensure the service rehearses all emergency response drills every three months.</li>
                        <li>Insist rehearsals will include lock down and evacuations.</li>
                        <li>Will arrange different scenarios to cover all possible emergency situations.</li>
                        <li>Time all rehearsals.</li>
                        <li>Ensure all rehearsals are documented on the register and signed by all staff.</li>
                        <li>Inform all staff, residents, volunteers and visitors that they are required to take part.</li>
                    </ul>
                    <h3><span style="color: #fcb53b;">Staff and volunteers will</span></h3>
                    <ul>
                        <li>Comply with the services emergency response procedure.</li>
                        <li>Participate in rehearsing lockdown and evacuations.</li>
                        <li>Respond to emergency situation and rehearsals with a sense of calmness.</li>
                        <li>Support, guide and ensure the residents feel safe and comfortable.</li>
                        <li>Ensure equipment and resources required for emergency responses are in working order/report to
                            workplace health and safety supervisor if repairs required.</li>
                    </ul>
                    <h3><span style="color: #fcb53b;">Emergency response Procedure</span></h3>
                    <h4><span style="color: #b67318;">Evacuation:</span></h4>
                    <ul>
                        <li>Alarm will be sounded by first person to identify the emergency.</li>
                        <li>If possible Residential Manager or Care Coordinator or closest person to call 000.</li>
                        <li>Collect fully charged phone and list of all staff names and contact.</li>
                        <li>Check room, foyer, kitchen, staff room and toilets for people.</li>
                        <li>Once evacuated check attendances of staff, medical professionals and visitors.</li>
                        <li>Follow emergency response direction.</li>
                        <li>If allowed back into the service do final attendance checks of staff, residents and visitors.
                        </li>
                        <li>Document evacuation and complete register.</li>
                        <li>Educator to collect medication records, medication, phone and sign in/ out sheets for children.
                        </li>
                        <li>Calmly encourage residents to line up at the door. One staff member at front of line, one at
                            back and rest of staff in-between.</li>
                        <li>Wheel those in need of assistance out in wheelchairs or beds if able.</li>
                        <li>Attendance check for visitors, residents and staff at door.</li>
                        <li>Staff member to collect the emergency bag.</li>
                        <li>Do a final check of room, toilets and cupboards/hiding spots.</li>
                        <li>Following the emergency plan at the exit walk calmly to the assembly point in the carpark.</li>
                        <li>Attendance check of staff, residents and visitors again. If anyone is missing report to
                            emergency crew immediately – do not re-enter the building.</li>
                        <li>Await direction from emergency crew and management.</li>
                    </ul>
                    <p>For more specific information on evacuation and response actions related to a Natural Disaster –
                        refer to SE4 Workplace Health and Safety Policy and Procedure.</p>
                    <h4><span style="color: #b67318;">Lockdown:</span></h4>
                    <ul>
                        <li>Alarm will be sounded by first person to identify the emergency.</li>
                        <li>If possible Resident Manager or Care Coordinator to call 000.</li>
                        <li>Lock all doors and close blinds including the foyer, staffroom, toilet, laundry and kitchen.
                        </li>
                        <li>Move staff and residents into the corner of the dining hall and remain until guided by emergency
                            services.</li>
                        <li>Instruct anyone with a mobile phone to put it on silent and non-vibrate.</li>
                        <li>If outside, relocate residemts to the closest inside secure area.</li>
                        <li>If on an excursion, do not return to school. Relocate children to St. Patricks Residential Care.
                        </li>
                    </ul>
                    <h4><span style="color: #b67318;">Bomb Threat:</span></h4>
                    <p>If received by phone:</p>
                    <ul>
                        <li>Stay Calm.</li>
                        <li>Keep caller talking.</li>
                        <li>Hold up four fingers to alert other staff of the nature of the call.</li>
                        <li>Other staff must immediately move away from any window and use mobile phones to call the
                            Residential Manager or Care Coordinator who will notify police. If either cannot be reached,
                            they must call police direct.</li>
                        <li>If able, write down as much as possible about the conversation with the caller.</li>
                        <li>Keep talking until you receive further instructions from authorities or caller hangs up.</li>
                        <li>Evacuate when directed.</li>
                    </ul>

