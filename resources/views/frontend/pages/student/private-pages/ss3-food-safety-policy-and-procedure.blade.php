                    <h2><span style="color: #da591f;">Food Safety Procedure</span></h2>
                    <ul></ul>
                    <h3><span style="color: #fcb53b;">Management will:</span></h3>
                    <ul>
                        <li>ensure staff comply with the policy</li>
                        <li>devise and monitor the compliance of food safety practices</li>
                        <li>supply resources required such as aprons, gloves and hand wash</li>
                        <li>ensure meal preparation areas, storing and cooking facilities are in working order</li>
                        <li>provide further training in food safety and handling</li>
                        <li>promote a culture that values safe food practices.</li>
                    </ul>
                    <h3><span style="color: #fcb53b;">Staff will:</span></h3>
                    <ul>
                        <li>comply with the food safety policy</li>
                        <li>apply the essence of the policy to all age groups making suitable amendment were necessary</li>
                        <li>be aware of medical conditions and act accordingly</li>
                        <li>supervise clients eating at all times</li>
                        <li>encourage clients to maintain personal hygiene, such as washing hands and not sharing food</li>
                        <li>comply with all safe food practice procedures as above.</li>
                    </ul>
                    <h3><span style="color: #fcb53b;">Chef will:</span></h3>
                    <ul>
                        <li>comply with the food safety policy</li>
                        <li>comply with all safe food practice procedures as above</li>
                        <li>manage all running of tasks in the kitchen</li>
                        <li>engage in honest and genuine practices</li>
                        <li>purchase produce for the service</li>
                        <li>store, prepare and serve food that caters for all meals and rooms</li>
                        <li>clean and maintain any hygienic kitchen</li>
                        <li>report any concerns or challenges to management</li>
                        <li>work in collaboration with management, staff, health and safety officers, and families</li>
                        <li>complete all required documentation and file appropriately.</li>
                    </ul>
                    <h3><span style="color: #fcb53b;">In relation to preparing and serving food:</span></h3>
                    <ul>
                        <li>Always wash hands before and after handling food.</li>
                        <li>Wear gloves whenever touching food with your hands.</li>
                        <li>Gloves are not required when using a clean utensil to serve or feed.</li>
                        <li>Separate cutting board should be used for raw meat, fruit and vegetables and dairy products.
                        </li>
                        <li>Wash your hands in-between handling different foods.</li>
                        <li>Meats must be thawed within the fridge and cooked and served on the day of thawing.</li>
                        <li>Always a hair net and apron when preparing food.</li>
                        <li>Serve food within 20 minutes of cooking or heating.</li>
                        <li>Serve food covered and in a clean sterilised tableware.</li>
                        <li>Provide each consumer with their own tableware and cutlery and replace this immediately should
                            it be compromised.</li>
                        <li>Never use chemicals around food that is out in the open.</li>
                        <li>Wash knives after use with uncooked meat and fish, and before use with any foods that are ready
                            to be eaten.</li>
                        <li>Ensure food is cooked or reheated to the correct temperature. Food should only be reheated once.
                        </li>
                    </ul>
                    <h3><span style="color: #fcb53b;">In relation to storing food:</span></h3>
                    <ul>
                        <li>dry foods should be stored in the original container or airtight containers in the pantry at
                            room temperature</li>
                        <li>dry food not in the original package should be labelled and dated</li>
                        <li>raw meats should be stored separately in sealed bags or containers in the fridge preferable on
                            the bottom shelf</li>
                        <li>fresh vegetables and fruits should be stored away from cooked foods preferably on top shelf</li>
                        <li>food beyond its used-by date should be discarded</li>
                        <li>when storing cooked food, ensure food has time to cool down, is stored in an airtight container,
                            then dated and refridgerated as required</li>
                        <li>no foods are to be stored in open spaces such as benches</li>
                        <li>if packages are broken when received discard</li>
                        <li>meats must be frozen within 24-48 hours of receiving</li>
                        <li>other items must be frozen within an appropriate time frame</li>
                        <li>pureed foods are to be labelled and stored on separate shelf and labelled according to their
                            National Dysphagia Diet level.</li>
                    </ul>
                    <h3><span style="color: #fcb53b;">In relation to freezer and fridge temperatures:</span></h3>
                    <ul>
                        <li>the temperature of all fridges should be 5 degrees or below</li>
                        <li>the temperature of all freezers should be -15 degrees</li>
                        <li>temperatures should be checked via a thermometer daily and documented on the temperature check
                            register.</li>
                    </ul>
                    <h3><span style="color: #fcb53b;">In relation to cooking food:</span></h3>
                    <ul>
                        <li>Food should always be cooked in relation to its specific instructions as per the label. Where
                            this is unclear, attempts must be made to research and educate on the correct cooking technique.
                        </li>
                    </ul>
                    <h3><span style="color: #fcb53b;">In relation to cleaning and disposing of food:</span></h3>
                    <ul>
                        <li>chef will place food scraps in the compost or waste bin immediately after cooking</li>
                        <li>each room will have its own scrap box that consumers or staff will dispose of leftover food into
                            and this will be placed into the compost bin or sent back to the kitchen</li>
                        <li>all foods past the expiry date will be discarded</li>
                        <li>foods that a spoiled or contaminated to be discarded</li>
                        <li>gloves are to be used when touching used dishes, which are to be rinsed and cleaned in the
                            dishwasher.</li>
                    </ul>
                    <h3><span style="color: #fcb53b;">In relation to managing choking risks:</span></h3>
                    <p>Particular food items pose greater choking risks for the elderly or those with dysphagia and
                        aspiration issues and extra care should be taken with these foods.</p>
                    <p>Common foods that may cause choking include:</p>
                    <ul>
                        <li>hard food that can break into smaller lumps or pieces</li>
                        <li>raw carrot, celery and apple pieces, which should be grated, finely sliced, cooked or mashed to
                            prevent choking</li>
                        <li>nuts, seeds and popcorn</li>
                        <li>tough or chewy pieces of meat</li>
                        <li>sausages, which should have the skin removed and be cut into small pieces to prevent choking.
                        </li>
                    </ul>
                    <p>All foods and liquids, for those with dysphagia and aspiration issues who cannot swallow normally,
                        should be pureed and thinned or thickened according to the National Dysphagia Diet level assigned
                        for each consumer.</p>
                    <h3><span style="color: #fcb53b;">In relation to dysphagia, aspiration, anaphylaxis, allergies and
                            intollerances:</span></h3>
                    <p>When preparing, cooking and serving food we must ensure:</p>
                    <ul>
                        <li>chefs and staff are aware of each consumer’s medical needs and have access to their management
                            plan</li>
                        <li>food is prepared and cooked separately from foods that trigger the medical condition</li>
                        <li>separate equipment must be used to prepare and cook the food</li>
                        <li>extra attention is placed on cleaning equipment used for trigger foods</li>
                        <li>food must be served and sealed in separate tableware and clearly labelled for the intended
                            consumer</li>
                        <li>staff should serve those consumers with a medical condition their food first</li>
                        <li>staff should sit near clients and supervise to ensure to the client does not consume the trigger
                            food</li>
                        <li>staff should check allergy and medical conditions on the board every mealtime, especially casual
                            staff.</li>
                    </ul>

