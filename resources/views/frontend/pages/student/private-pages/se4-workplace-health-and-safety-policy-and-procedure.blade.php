                    <h2><span style="color: #da591f;">Workplace Health and Safety Procedure</span></h2>
                    <ul></ul>
                    <h3><span style="color: #fcb53b;">Management will ensure the following:</span></h3>
                    <ul>
                        <li>Regularly consult with the team wherever practical on all WHS issues and regulatory or workplace
                            changes that may affect their workplace.</li>
                        <li>Be proactive in identifying any potential hazards or risks and implement control measures.</li>
                        <li>Ensure Banksia Care complies with all State guidelines and regulatory requirements in relation
                            to WHS.</li>
                        <li>Attend any professional training or leadership workshops related to WHS to ensure staff are
                            aware and informed of industry changes and trends.</li>
                        <li>Regularly review Emergency and Risk Management practices.</li>
                        <li>Investigate incidents to prevent any reoccurrence and implement the change required.</li>
                        <li>Provide WHS training and information for all staff.</li>
                        <li>Provide and maintain safe resources and residential rooms, facilities and equipment.</li>
                        <li>Support staff by providing wellness support staff to ensure mental health is supported as well
                            as physical.</li>
                    </ul>
                    <ul></ul>
                    <h3><span style="color: #fcb53b;">In relation to managing accidents, all staff will refer to:</span>
                    </h3>
                    <ul>
                        <li>Banksia Care PC2 – Incident, Injury, Illness and Trauma Policy and Procedure</li>
                        <li>Banksia Care P.C. T1 – Incident, Injury, Illness and Trauma Record</li>
                        <li>Banksia Care S.E. T2 – Serious Incident Report Form</li>
                        <li>Banksia Care SE5 – Risk Management Policy and Procedure</li>
                        <li>Banksia Care S.E. T1 – Hazard Report Form</li>
                        <li>Banksia Care S.E. D1 – Emergency Management Plan</li>
                    </ul>
                    <ul></ul>
                    <h3><span style="color: #fcb53b;">In relation to medical emergencies, all staff will refer to:</span>
                    </h3>
                    <ul>
                        <li>Banksia Care PC1 – Infection Control Policy and Procedure</li>
                        <li>Banksia Care PC3 – Administration of Medication Policy and Procedure</li>
                        <li>Banksia Care P.C. T3 – Medication Administration Log</li>
                        <li>Banksia Care P.C. T2 – Medication Authority Form</li>
                        <li>Banksia Care PC T4 – Referral Form</li>
                        <li>Banksia Care P.C. T1 – Incident, Injury, Illness and Trauma Record</li>
                        <li>Banksia Care PC2 – Incident, Injury, Illness and Trauma Policy and Procedure</li>
                    </ul>
                    <ul></ul>
                    <h3><span style="color: #fcb53b;">In relation to chemical spills/toxic emissions, all staff will follow
                            the steps below:</span></h3>
                    <ul></ul>
                    <h4><span style="color: #b67318;">In the event of a significant event (such as explosion, rolled
                            tanker):</span></h4>
                    <ul>
                        <li>If they witness the event, contact Residential Manager or Care Coordinator, who will notify
                            police.</li>
                        <li>Move everyone inside immediately, utilising rooms/buildings furthest away from the incident.
                        </li>
                        <li>Close all external doors and windows.</li>
                        <li>Ensure air conditioners are turned off.</li>
                        <li>Any naked flames (if in kitchens or science areas) are turned off upon exit.</li>
                        <li>Await all clear or further instructions from authorities.</li>
                        <li>Only evacuate premises if advised to by police or emergency workers.</li>
                    </ul>
                    <ul></ul>
                    <h4><span style="color: #b67318;">In the event of a minor event (such as a garden/cleaning incident or
                            workshop incident):</span></h4>
                    <ul>
                        <li>Move everyone to a safe area.</li>
                        <li>Check if anyone was injured or needs medical attention.</li>
                        <li>Staff not attending to residents to use protective clothing to clean spill.</li>
                        <li>When the Residential Manager or Care Coordinator is satisfied the area is secure and clean staff
                            can return residents to normal activities.</li>
                        <li>Record incident on a Hazard Report Form.</li>
                    </ul>
                    <ul></ul>
                    <h3><span style="color: #fcb53b;">In relation to a natural disaster, all staff will ensure the
                            following:</span></h3>
                    <ul></ul>
                    <h4><span style="color: #b67318;">In the event of an earthquake:</span></h4>
                    <p>If an earthquake occurs whilst inside:</p>
                    <ul>
                        <li>Stay inside and ensure you remain in your current location.</li>
                        <li>Do not move around the premises, separate residents if in a group location or attempt to leave
                            the building.</li>
                        <li>Instruct residents (if able) and staff to get under tables, benches or stand in internal door
                            frames.</li>
                        <li>Keep everyone away from windows, shelves and overhead fittings.</li>
                    </ul>
                    <p>If an earthquake occurs whilst outside:</p>
                    <ul>
                        <li>Keep residents and staff clear of buildings, retainer/garden walls, power lines, trees and
                            anything else that may fall and cause injury or hazard.</li>
                        <li>Do not separate any group.</li>
                    </ul>
                    <p>After the earthquake:</p>
                    <ul>
                        <li>Check everyone for any injuries and administer first aid.</li>
                        <li>Do not move seriously injured residents or staff unless you believe they are in imminent danger.
                        </li>
                        <li>If able, turn off electricity, gas and water.</li>
                        <li>If inside, evacuate everybody outside, away from buildings, trees and power lines by the safest
                            route. Refer to Banksia Care’s “Evacuation Procedure”.</li>
                        <li>Contact the Residential Manager or Care Coordinator by mobile phone.</li>
                        <li>Follow any advice or instruction given by local authorities or emergency crew.</li>
                        <li>Be on alert for aftershocks. Never re-enter buildings even if no visible damage until declared
                            safe to do so by emergency authorities.</li>
                        <li>If the building is damaged and needs to be closed – arrange alternate care for residents.</li>
                    </ul>
                    <ul></ul>
                    <h4><span style="color: #b67318;">In the event of a flood:</span></h4>
                    <p>Staff are required to be aware of previous flood levels and stay informed about potential flood
                        threats.</p>
                    <p>If sufficient warning is received, ensure the following:</p>
                    <ul>
                        <li>Next of kin/family of residents will be contacted to assist with alternate care within the hour.
                            If unable to be cared for by family in the short term, or if medical assistance is required,
                            Banksia Care will source alternate accommodation.</li>
                        <li>If roads to the service are already flooded, Banksia Care will arrange accommodation for those
                            stranded.</li>
                        <li>Early release will be arranged for employees who are at risk of being cut off from accessing
                            homes or have properties threatened.</li>
                        <li>Ensure electricity and gas are turned off prior to final evacuation.</li>
                        <li>If time allows and it is safe to do so, move what you can to higher points:
                            <ul>
                                <li>Stack furniture and books above likely flood levels with electrical equipment on top.
                                </li>
                                <li>B. At no time should equipment or resources take priority over the safety and wellbeing
                                    of residents, staff or volunteers.</li>
                            </ul>
                        </li>
                    </ul>
                    <p>If no warning is received, or insufficient time is given to evacuate:</p>
                    <ul>
                        <li>Safety of all at Banksia Care is priority. No time should be spent on collecting items, stacking
                            items, turning off utilities, or calling family members. Emergency medical bag / first aid kit
                            should be taken only.</li>
                        <li>If time allows, the service will be evacuated immediately, and buses will take residents to the
                            closest high safety zone previously identified. In the case of Bankia Care, this is Patricks
                            Residential Care on Markspoint Avenue. For those requiring medical assistance, ambulances will
                            be called.</li>
                        <li>If evacuation is not possible by bus option, move residents immediately to the highest point in
                            the service, that being the Merrick Chapel.</li>
                        <li>Remain tuned in to a radio/phone line to SES and follow the advice given.</li>
                    </ul>
                    <p>After the flood:</p>
                    <ul>
                        <li>If Banksia Care is closed for an extended period of time, arrange alternate care for residents.
                        </li>
                    </ul>
                    <ul></ul>
                    <h3><span style="color: #fcb53b;">In relation to an invacuation (lockdown), all staff will follow the
                            procedures below:</span></h3>
                    <p>Staff are to immediately ensure the following:</p>
                    <ul>
                        <li>Speak to the Residential Manager or Care Coordinator immediately by phone call.</li>
                        <li>Remain with residents and keep everyone as calm as possible.</li>
                        <li>Keep everyone away from windows and doors.</li>
                        <li>Secure all doors.</li>
                        <li>Instruct anyone with a mobile phone to put it on silent and non-vibrate.</li>
                        <li>If outside, relocate residents to the closest inside the secure area.</li>
                        <li>If on an external outing, do not return to Banksia Care. Relocate residents to St. Patricks
                            Residential Care.</li>
                        <li>Remain in the secure area until further instruction from authorities or on advice threat has
                            passed.</li>
                    </ul>
                    <ul></ul>
                    <h3><span style="color: #fcb53b;">In relation to a bomb threat, all staff will:</span></h3>
                    <p>If received by phone:</p>
                    <ul>
                        <li>Stay calm.</li>
                        <li>Keep the caller talking.</li>
                        <li>Hold up four fingers to alert other staff of the nature of the call.</li>
                        <li>Other staff must immediately move away from any window and use mobile phones to call the
                            Residential Manager or Care Coordinator, who will notify police. If neither can be reached, they
                            must call the police direct.</li>
                        <li>If able, write down as much as possible about the conversation with the caller.</li>
                        <li>Keep talking until you receive further instructions from authorities or caller hangs up.</li>
                        <li>Evacuate when directed.</li>
                    </ul>

