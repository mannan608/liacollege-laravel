                    <h2><span style="color: #da591f;">Policy Statement</span></h2>
                    <p>The purpose of the Referrals Policy is to document the steps staff are expected to follow when
                        referrals to health care specialists are needed. This ensures the process is consistent and that the
                        needs of the residents are being met in a timely manner. Having a clear policy in place helps to
                        ensure that residents receive the appropriate care and support from specialists and that any issues
                        are identified and addressed early on.</p>
                    <p>This documentation ensures residents receive appropriate care and support from specialists while also
                        proactively addressing potential issues. Additionally, a documented referral policy helps the
                        facility meet regulatory standards and provide the best possible care for residents.</p>
                    <ul></ul>
                    <h2><span style="color: #da591f;">Scope</span></h2>
                    <p>This policy applies to Banksia Care consumers, representatives of consumers, external service
                        providers, Banksia Care board members, staff, management, and medical professionals. The policy
                        applies to aged care services provided as supported independent living and those residing in Banksia
                        aged care and disability care facilities.</p>
                    <ul></ul>
                    <h2><span style="color: #da591f;">Background</span></h2>
                    <p>Banksia Care embeds a person-centred care approach to service delivery. This requires prioritising
                        the consumer’s aspirations and strengths within the context of their capacity. In supporting aged
                        care residents, Banksia staff place the needs of people in our care first. Critical to this approach
                        is careful observation to identify changes in behaviour that may indicate that a resident requires
                        additional support. As people age, changes in physical and mental health occur.</p>
                    <p>Ensuring that people in Banksia’s care receive a referral to specialist services to support them
                        across the ageing journey is vital to Banksia’s person-centred care approach.</p>
