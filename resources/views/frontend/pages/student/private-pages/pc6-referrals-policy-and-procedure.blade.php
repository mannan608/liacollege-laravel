                    <h2><span style="color: #da591f;">Referrals Procedure</span></h2>
                    <ul></ul>
                    <h3><span style="color: #fcb53b;">Who can request a referral?</span></h3>
                    <p>Banksia Care facilities will always have registered nurses working in their facilities in shifts.
                        Banksia nominates one nurse to be the RNIC or registered nurse in charge. Any support staff who
                        observe a need to refer a person to specialist support are to:</p>
                    <ul>
                        <li>Note the observation or request from the person being cared for in Banksia’s client management
                            system, known as Autumn Care, in the person’s care plan.</li>
                        <li>They are then to raise the referral with the RNIC directly in person.</li>
                        <li>The RNIC then evaluates the person. If a referral is needed, the Banksia support staff who
                            initiated the referral will make an appointment with:
                            <ul>
                                <li>the person’s preferred specialist; or</li>
                                <li>Banksia Care’s approved specialists.</li>
                            </ul>
                        </li>
                    </ul>
                    <p>Referrals respond to the needs of people, and in cases where the person is suffering from pain or
                        discomfort, assessments are to be conducted as a priority and asap with the agreement of the person
                        or their representative.</p>
                    <ul></ul>
                    <h3><span style="color: #fcb53b;">Essential:</span></h3>
                    <ul>
                        <li>Ensure that before a request for a referral is made, the person in care or their representative
                            (in cases where the person has another person legally acting on their behalf) is consulted to
                            ensure their opinion and preferences are considered and remain paramount.</li>
                        <li>In emergency situations, immediate steps must be taken to support the person in our care.</li>
                    </ul>
                    <p>Note: respect DNR (Do Not Resuscitate) orders.</p>
                    <ul></ul>
                    <h3><span style="color: #fcb53b;">Procedure:</span></h3>
                    <ul>
                        <li>Support staff log a need for a referral in the care plan for the person in Banksia’s Client
                            Management System (Autumn Care), but a copy is not needed to be placed in the client’s actual
                            file.</li>
                        <li>Always include the client’s full name and date of birth, plus a detailed summary of the reason
                            for referral.</li>
                        <li>When completing the referral request, ensure you don’t use emotive language or convey your own
                            feelings regarding the situation. Referrals must be based on facts and direct observation only.
                        </li>
                        <li>A duty RNIC visits with the person and determines if a referral is required. The RNIC may
                            consult with doctors and other specialists when making any decision. Support staff, along with
                            the RNIC, ensure the person being cared for is involved in the requirement for referrals.</li>
                        <li>On the approval of the RNIC, Banksia staff are to arrange an appointment. In some cases, people
                            will have their own preferences for specialists (such as a dentist), which will be recorded in
                            their care plan. Appointments are recorded in Autumn Care and in the start of shift diary that
                            support staff refer to.</li>
                        <li>Specialists will require information about the referral and the service required. Specialists
                            may visit on-site and use our facilities to provide services.</li>
                        <li>Banksia Care staff will ensure that the person in our care is prepared to meet appointments. At
                            the start of a shift and at handover, staff will:
                            <ul>
                                <li>read of any planned appointments in the start of shift diary</li>
                                <li>be advised of appointments by the shift handing over.</li>
                            </ul>
                        </li>
                        <li>People in our care may require or need support during referrals. Banksia Care staff are to
                            ensure people are assisted as needed.</li>
                    </ul>

