                    <h3><span style="color: #fcb53b;">In Relation to Consumer Needs</span></h3>
                    <ul>
                        <li>Banksia Care has a culture of inclusion and respect for consumers as people. As a result, people
                            must be informed of and agree to undertake referral for specialist services.</li>
                        <li>Banksia Care supports consumers to exercise choice and independence and actively partners with
                            the consumer to inform ongoing care assessment and planning. Banksia acknowledges a Dignity of
                            Risk that allows people in our care to make informed decisions about recommended referrals.</li>
                        <li>Banksia Care will carry out an ongoing assessment and planning with the consumer, their official
                            representatives, and others who the consumer wants to be involved in their care and services
                            assessment and planning. Referrals are made to improve the health and lives of the people in our
                            care.</li>
                        <li>Consistent and ongoing sharing of information, asking for feedback from the consumer, and
                            supporting and encouraging consumers to take part in assessing and planning their own care and
                            services are paramount and aligned with a person-centred practice approach. Referrals are
                            recommended by authorised staff based on the principle of improving health outcomes. Referrals
                            can be supported by documented observations, incidents and a person’s own requests that are
                            recorded in Banksia’s consumer management system. Banksia relies on a list of approved
                            specialist service providers who are provided access to relevant recorded information to support
                            the health needs of the person in our care.</li>
                        <li>A person-centred approach recognises that making decisions about their own life, and having
                            those decisions respected, is an essential right of each consumer. It improves their health and
                            wellbeing and demonstrates the organisation’s values to the consumer.</li>
                        <li>The consumer may choose to have a relative, partner, or friend as a representative involved in
