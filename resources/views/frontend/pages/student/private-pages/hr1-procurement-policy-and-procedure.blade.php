                    <h3><span style="color: #fcb53b;">Professional Development</span></h3>
                    <p>Professional development refers to formal or informal training and may include:</p>
                    <ul>
                        <li>networking with other services and professionals</li>
                        <li>mentoring and coaching programs</li>
                        <li>in-house or external training (workshops, courses)</li>
                        <li>sharing information gained from formal studies</li>
                        <li>conferences</li>
                        <li>visitors from local areas</li>
                        <li>reading professional publications and websites</li>
                        <li>viewing professional DVDs</li>
                        <li>engage in professional reflection (journals)</li>
                        <li>further formal study.</li>
                    </ul>
                    <p>Professional development within Banksia Care:</p>
                    <ul>
                        <li>Management will arrange a variety of professional development opportunities throughout the year.
                        </li>
                        <li>A variety of professional development types will be provided to suit all staff.</li>
                        <li>Concepts and topics will be decided based on the staff and manager’s needs at the time.</li>
                        <li>Professional development will not be at the cost of Banksia Care employees or volunteers.</li>
                        <li>Managers and staff must engage in three professional development opportunities per year.</li>
                        <li>Managers and staff are encouraged to implement new skills and knowledge within the service.</li>
                        <li>Managers and staff must engage in professional and respectful practice during professional
                            development – such as respectful behaviour and arriving on time.</li>
                    </ul>
                    <h3><span style="color: #fcb53b;">Staff Annual Appraisal</span></h3>
                    <p>Staff appraisals are an opportunity to reflect on your role and duties, highlight achievements, and
                        consider your strengths and areas to continue to work on to meet your full potential. Staff will
                        meet with management and gain positive and constructive feedback through a genuine and collaborative
                        discussion regarding your role.</p>
                    <ul>
                        <li>All staff will undergo a staff appraisal every year. This will be conducted at the same time
                            each year.</li>
                        <li>New staff will be introduced to the formal performance management system during orientation.
                        </li>
                        <li>An initial performance appraisal will take place within three months of appointment to ensure
                            new staff are clear about their responsibilities and their services expectations, and thereafter
                            at least every 12 months.</li>
                        <li>Staff will be informed two weeks before appraisal and will be given a self-reflection template
                            to fill out and return before the appraisals.</li>
                        <li>The appraisal will include the Residential Manager, the Care Coordinator, the Education
                            Coordinator and yourself.</li>
                        <li>During the appraisal, you can expect to discuss strengths and achievements, challenges, concerns
                            and areas you would like to develop in and areas management would like to support you with.</li>
                        <li>In order to facilitate a review of overall performance, reference will be made to each staff
                            member’s job description.</li>
                        <li>In determining the objectives, what might reasonably be expected of any staff member at the
                            stage in their career, their aspirations and work-life balance, should be borne in mind.</li>
                    </ul>
                    <h3><span style="color: #fcb53b;">Performance Management Process (in the Event a Breach has
                            Occurred)</span></h3>
                    <ul>
                        <li>Informal complaints management happens when staff receive feedback about their performance from
                            their work colleagues, a resident or a visitor but does not include a breach.</li>
                        <li>Issues or concerns will be addressed straight away, thus preventing the development of conflict
                            or major problems.</li>
                        <li>Banksia Care will comply with the ‘Customer Complaints Policy’ in order to manage the complaint.
                        </li>
                    </ul>
                    <p>In the event a staff member makes a breach of a Regulation or Policy, the following will be
                        implemented:</p>
                    <ul>
                        <li>A proposed breach has been indicated to the management team.</li>
                        <li>The Residential Manager will inform the accused that an investigation is underway and provide
                            them with time to prepare for an interview.</li>
                        <li>The Residential Manager will interview the accused, discuss the allegations, and allow the staff
                            member to state their side. This will be unbiased and fair and in pursuit of the truth.</li>
                        <li>The accused staff member will be allowed to have one person sit with them in the meeting.</li>
                        <li>The agreed time for resolving the issue will be confirmed.</li>
                        <li>The Residential Manager will start an immediate investigation. This will include interviewing
                            all parties, gathering evidence, and mediating between parties if needed.</li>
                        <li>The Residential Manager will come to an outcome regarding whether a breach has occurred.</li>
                        <li>If a breach occurs, the Residential Manager will give the staff member an official written
                            warning (unless it is a fireable offence).</li>
                        <li>The Residential Manager and staff member will devise a Performance Management Action Plan to
                            ensure the staff member develops the skill and knowledge required around the area where the
                            breach occurs.</li>
                        <li>The staff member and the Residential Manager will meet every two weeks to discuss the training
                            plan until Residential Manager is satisfied with the learning outcome.</li>
                        <li>If the Residential Manager is unavailable, the Care Coordinator may be required to manage the
                            above process.</li>
                    </ul>
                    <h3><span style="color: #fcb53b;">Termination</span></h3>
