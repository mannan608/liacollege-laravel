                    <h2><span style="color: #da591f;">Principles that Inform our Policy</span></h2>
                    <p>All decision-making about our <em>Duty of Care Procedure</em> is carried out in accordance with the
                        principles of our <em>Duty of Care Policy</em>.</p>
                    <ul>
                        <li>We understand that activities involve different levels of risk.</li>
                        <li>Particular care may need to be taken to support residents and patients with additional needs.
                        </li>
                        <li>It is imperative that the facilities premises are kept in good repair and free from hazards. The
                            organisation will take any reasonable measure to reduce the risk to all stakeholders.</li>
                        <li>Everyone is encouraged to speak to the care or concerns regarding the safety or wellbeing of
                            self or others.</li>
                        <li>We acknowledge that, as our duty of care is non-delegable, we are also required to take
                            reasonable steps to reduce the risk of reasonably foreseeable harm when external providers have
                            been engaged to plan for or conduct an activity involving our residents.</li>
                    </ul>
