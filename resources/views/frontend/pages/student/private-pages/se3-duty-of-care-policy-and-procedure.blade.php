                    <h2><span style="color: #da591f;">Workplace Health and Safety Procedure</span></h2>
                    <ul></ul>
                    <h3><span style="color: #fcb53b;">Management will ensure the following:</span></h3>
                    <ul>
                        <li>Take reasonable steps to minimise the risk of reasonably foreseeable harm, including:
                            <ul>
                                <li>ensuring the facility complies with the aged care policies and legislation</li>
                                <li>provision of suitable and safe premises</li>
                                <li>provision of an adequate system of supervision</li>
                                <li>implementation of strategies to prevent bullying and harassment</li>
                                <li>ensuring that medical assistance is provided to sick or injured patients and staff</li>
                                <li>managing employee recruitment, conduct and performance.</li>
                            </ul>
                        </li>
                        <li>Inform all staff of their Duty of Care as part of Banksia Care’s Induction Program and ongoing
                            Professional Development.</li>
                        <li>Ensure ‘Duty of Care’ will be an agenda item at staff meetings.</li>
                        <li>Ensure staff completes a risk assessment, including duty of care when completing planning for
                            excursions, special events and incursions.</li>
                        <li>Direct staff to the Wellness and Support policy, annually.</li>
                        <li>Be responsible for making and administering supervision arrangements as necessary according to
                            the circumstances and ensure staff are responsible for carrying out their assigned supervisory
                            duties so that residents and patients are, as far as can be reasonably expected, protected from
                            injury.</li>
                        <li>Develop procedures to manage common risks in the facilities environment, including:
                            <ul>
                                <li>first aid</li>
                                <li>grounds maintenance</li>
                                <li>ICT protection</li>
                                <li>emergency management</li>
                                <li>volunteers/visitors</li>
                                <li>mandatory reporting</li>
                                <li>workplace health and safety</li>
                                <li>air contaminants</li>
                                <li>chemical spills</li>
                                <li>dust</li>
                                <li>waste.</li>
                            </ul>
                        </li>
                    </ul>
                    <h3><span style="color: #fcb53b;">Staff will ensure the following:</span></h3>
                    <ul>
                        <li>Protect residents and patients from reasonably foreseeable harm as a concurrent duty of care.
                        </li>
                        <li>Respect and agree that their duty of care is not confined to the facility’s geographic area,
                            activities, or activities occurring outside the facility where a resident is acting on a staff
                            member’s instructions.</li>
                        <li>Don’t give advice on matters they are not professionally competent to give (negligent advice).
                        </li>
                        <li>Use the phone to contact a supervisor in case of an emergency or if help is required.</li>
                        <li>Be aware that an incursion with an external provider does not absolve supervision duties of the
                            staff, including first aid duties.</li>
