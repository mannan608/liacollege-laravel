                    <h2><span style="color: #da591f;">Staff Health and Wellbeing Procedure</span></h2>
                    <p>The emphasis on social, emotional and physical wellbeing is crucial and emphasises that good social,
                        emotional and physical health is an important component in a holistic learning framework. Banksia
                        Care believes you cannot have a workforce to whom we entrust the care of patients who are not a
                        model of wellbeing. We, therefore, value, promote and strive to maintain a workplace that supports
                        and protects the positive physical and mental health of all staff.</p>
                    <p>Health and wellbeing programs currently implemented within the organisation. Where possible, these
                        programs will support local businesses.</p>
                    <ul>
                        <li>Lunchtime walking club.</li>
                        <li>Prescription to online yoga and meditation.</li>
                        <li>Study hub for staff enrolled in a relevant course of study to offer support and encouragement.
                        </li>
                        <li>Each staff member will receive a habits and gratitude diary.</li>
                        <li>Swap and share table – staff can bring in items/food/plants to swap, share or give away.</li>
                        <li>‘You Did It’ memo board – staff to write positive messages to each other.</li>
                        <li>Staff meeting – will open with sharing a positive story and moment of gratitude.</li>
                        <li>Professional development workshops provided based around health and wellbeing topics.</li>
                    </ul>
                    <h3><span style="color: #fcb53b;">Management will ensure the following:</span></h3>
                    <ul>
                        <li>Ensure all staff are aware of the service policy.</li>
                        <li>Ensure all staff are accepted and valued as individuals and professionals.</li>
                        <li>Cultivate a workplace that promotes connectedness and inclusion.</li>
                        <li>Respond immediately to behaviour that is not conducive to health and wellbeing.</li>
                        <li>Arrange Professional Development around health and wellbeing practices for all staff.</li>
                        <li>Provide connections to support services to refer staff to, such as a local GP or psychologist.
                        </li>
                        <li>Encourage staff to take adequate breaks/annual leave.</li>
                        <li>Ensure the implementation of the sun safety policy.</li>
                        <li>Be open to discussions and conversations about staff’s challenges.</li>
                        <li>Ensure confidentiality and privacy are maintained.</li>
                        <li>Implement a tobacco, alcohol and drug-free environment.</li>
                    </ul>
                    <h3><span style="color: #fcb53b;">Staff will ensure the following:</span></h3>
                    <ul>
                        <li>Comply with the policy.</li>
                        <li>Support the policy by making decisions that optimise support for health and wellbeing.</li>
                        <li>Be respectful to each other.</li>
                        <li>Where appropriate, participate in health and wellbeing practices and events.</li>
                        <li>Be open about challenges and concerns within the organisation.</li>
                        <li>Seek a health professional when needed.</li>
                        <li>Make choices that encourage a healthy work-life balance.</li>
