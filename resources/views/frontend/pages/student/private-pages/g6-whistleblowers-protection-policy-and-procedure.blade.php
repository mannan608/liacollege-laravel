                    <h2><span style="color: #da591f;">Community Engagement Procedure</span></h2>
                    <ul></ul>
                    <h3><span style="color: #fcb53b;">Management will ensure the following:</span></h3>
                    <ul>
                        <li>Appoint a Whistleblower Protection Officer (WPO) who will have specific responsibilities under
                            this policy.</li>
                        <li>Ensure that all persons at Banksia who identify suspected or actual misconduct are encouraged to
                            disclose in confidence, knowing they will be supported and protected.</li>
                        <li>Deter any misconduct through Banksia Care’s risk management framework.</li>
                        <li>Be proactive in identifying any potential hazards or risks and implement control measures.</li>
                        <li>Ensure Banksia Care complies with all State guidelines and regulatory requirements concerning
                            the Whistleblower Law 2019.</li>
                        <li>Attend any professional training or leadership workshops related to whistleblowing to ensure
                            staff are aware and informed of industry changes and trends.</li>
                        <li>Regularly review all related policies and procedures.</li>
                        <li>Investigate incidents to prevent any reoccurrence and implement the change required.</li>
                        <li>Provide reporting training and information for all staff.</li>
                        <li>Conduct disciplinary action against anyone who has caused detriment to another for speaking out.
                        </li>
                        <li>Assist staff by providing wellness support to ensure mental health is looked after during the
                            process.</li>
                    </ul>
                    <h3><span style="color: #fcb53b;">Residents, staff, medical professionals and visitors will ensure the
                            following:</span></h3>
                    <ul>
                        <li>Ensure they have reasonable grounds when reporting suspected misconduct, but understand they
                            will not be penalised if the allegation is proven to be untrue.</li>
                        <li>Never participate in, or engage in any threatening behaviours toward anyone who has spoken out.
                        </li>
                        <li>Be aware that personal work-related issues or grievances are not related to the Whistleblowers
                            Policy.</li>
                        <li>In the first instance, report any suspected misconduct to the Whistleblower Protection Officer.
                        </li>
                        <li>Decide whether to speak up and be identified, confidentially or anonymously.</li>
                        <li>Speak up in confidence, knowing they will be supported and protected without repercussions.</li>
                        <li>Understand that they may receive protection from civil, criminal, and administrative liability
                            (including disciplinary action) in respect of the disclosure.</li>
                        <li>Be kept informed of and updated during any investigation where your identity is known and where
                            it is deemed appropriate.</li>
                    </ul>
                    <h3><span style="color: #fcb53b;">Examples of misconduct that may require a person to speak out:</span>
                    </h3>
                    <ul>
                        <li>Abuse, neglect or intent to cause harm</li>
                        <li>Breach of regulatory requirement</li>
                        <li>Bribery</li>
                        <li>Conflicts of interest</li>
                        <li>Conduct that may endanger the life or health of another person</li>
                        <li>Corruption</li>
                        <li>Harassment</li>
                        <li>Modern slavery</li>
                        <li>Threats of attacks against a person, the business, the financial system, or security</li>
                        <li>Unethical or dishonest behaviour.</li>
                    </ul>
                    <h3><span style="color: #fcb53b;">Examples of misconduct that may require a person to speak out:</span>
                    </h3>
                    <ol>
                        <li>Speak out – Disclose suspected or actual misconduct to the WPO either as known, in confidence or
                            anonymously.</li>
                        <li>Investigate – The WPO will consult with management and determine if an investigation is
                            required. If so, the WPO will locate evidence to support or refute the claim, may consult with
                            external professionals, ensure confidentiality with information obtained, and seek cooperation
                            from any relevant employee or contractor.</li>
                        <li>Report – The WPO will report the findings of the investigation back to management. Give the
                            person being investigated an opportunity to respond (where appropriate), inform the
                            whistleblower of findings (if the whistleblower is known and where appropriate), and make
                            recommendations to the board or management.</li>
                    </ol>

