                    <h2><span style="color: #da591f;">Policy Statement</span></h2>
                    <p>Banksia Care is committed to maintaining the highest standards of integrity and ethical behaviour in
                        all our activities. As a provider of aged care services, we understand the importance of avoiding
                        conflicts of interest that could compromise the quality of care we provide to our residents.</p>
                    <p>This policy sets out the expectations and requirements for identifying and managing conflicts of
                        interest within our organisation.</p>
