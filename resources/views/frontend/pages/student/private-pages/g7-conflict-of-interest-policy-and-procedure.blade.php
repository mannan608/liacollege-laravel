                    <h2><span style="color: #da591f;">Scope</span></h2>
                    <p>This policy applies to staff, management, contractors, and volunteers of Banksia Care.</p>
                    <ul></ul>
                    <h2><span style="color: #da591f;">Background</span></h2>
                    <p>A conflict of interest arises when an individual or organisation has competing loyalties or interests
                        that could influence their decision-making or actions. Conflicts of interest can arise in many
                        different situations, such as personal relationships, financial interests, or outside activities.
                    </p>
                    <p>All Banksia Care employees and contractors have a responsibility to identify and manage any conflicts
                        of interest that may arise in the course of their work. This includes disclosing any conflicts of
                        interest to their supervisor/direct manager or the designated conflict of interest officer in
                        writing.</p>
