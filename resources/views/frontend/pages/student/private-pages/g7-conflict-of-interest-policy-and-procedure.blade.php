                    <h2><span style="color: #da591f;">Induction and Ongoing Training</span></h2>
                    <p>Banksia Care requires that induction and ongoing training of all staff include the Conflict of
                        Interest Policy to enable staff to fulfil their roles effectively.</p>
                    <p>Banksia Care promotes information sharing at staff meetings, sharing of information received from
                        industry trends or changes in legislation, and in consultation at policy review sessions.</p>
                    <ul></ul>
                    <h2><span style="color: #da591f;">Monitoring, Evaluation and Review</span></h2>
                    <p>This policy will be reviewed annually or on the occurrence of any relevant legislative change.
                        Management of Banksia Care will conduct reviews in consultation with the team at staff meetings.</p>
                    <ul></ul>
                    <h2><span style="color: #da591f;">Conflict of Interest Procedure</span></h2>
                    <ul></ul>
                    <h3><span style="color: #fcb53b;">Managing Conflicts of Interest</span></h3>
                    <p>If a conflict of interest is identified, Banksia Care will take appropriate steps to manage the
                        conflict, which may include:</p>
                    <ul>
                        <li>removing the individual from the decision-making process</li>
                        <li>requiring the individual to disclose the conflict of interest to the affected parties</li>
                        <li>modifying the individual’s responsibilities or duties</li>
                        <li>termination of employment or contract if deemed appropriate.</li>
                    </ul>
                    <h3><span style="color: #fcb53b;">Reporting Conflicts of Interest</span></h3>
                    <p>All employees and contractors are responsible for reporting any actual or potential conflicts of
                        interest to their supervisor/direct manager or the designated conflict of interest officer in
                        writing. Reports will be treated confidentially, and no employee will be penalised for reporting a
                        conflict of interest in good faith.</p>
                    <ul></ul>
                    <h3><span style="color: #fcb53b;">Prohibited Conduct</span></h3>
                    <p>The following conduct is strictly prohibited and will not be tolerated:</p>
                    <ul>
                        <li>accepting or offering gifts, gratuities, or entertainment that could influence or appear to
                            influence business decisions.</li>
                        <li>using Banksia Care resources or information for personal gain</li>
                        <li>engaging in business or financial transactions that conflict with Banksia Care’s interests</li>
                        <li>providing care to residents who are related to or have a personal relationship with the
                            employee, except in the case of emergency situations.</li>
                    </ul>

