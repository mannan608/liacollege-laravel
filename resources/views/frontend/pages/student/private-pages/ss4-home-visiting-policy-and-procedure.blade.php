                    <h2><span style="color: #da591f;">Home Visiting Procedure</span></h2>
                    <p>The following procedure allows staff employed by Banksia Care to carry out home visits with a high
                        level of safety.</p>
                    <ul></ul>
                    <h3><span style="color: #fcb53b;">Conduct Risk Assessment</span></h3>
                    <p>Prior to the first home visit and when circumstances change, an assessment is carried out, and an
                        assessment form is completed to evaluate any risks associated with the client and their home.</p>
                    <p>A formal assessment clarifies whether the risk is as follows:</p>
                    <ul>
                        <li>High/extreme risk: Do not visit the client – consider alternative arrangements (see Document
                            Risk Management Plan below).</li>
                        <li>Medium/significant risk: Two staff must visit the client until further assessments demonstrate
                            otherwise.</li>
                        <li>Low risk: Visit the client but always conduct a mini-risk assessment immediately prior to entry.
                        </li>
                    </ul>
                    <p>Clients with stable mental health will have the level of risk reassessed at their
                        management/individual plan review or where there is reason to believe that the level of risk has
                        changed.</p>
                    <p>Assessment, prevention and management planning should include the client, as well as the roles of
                        relevant others and their contributions.</p>
                    <ul></ul>
                    <h3><span style="color: #fcb53b;">Assess Risk</span></h3>
                    <p>When assessing risk:</p>
                    <ul>
                        <li>Two staff will conduct an initial assessment for risk of aggression, preferably at the
                            designated workplace if possible. If only one staff is available, measures must be in place for
                            the workers safety such as phoning the office before engaging the client and establishing safety
                            protocols.</li>
                        <li>Two staff should conduct the initial home visit during office hours, and during this visit, the
                            home assessment should be completed.</li>
                        <li>Workers who are uncertain as to the level of risk involved in a home visit must discuss the
                            situation with their supervisor before going to and/or entering the client’s home.</li>
                        <li>Consider the risk to male staff concerning possible allegations of sexual misconduct by female
                            clients, particularly in the privacy of a client’s home.</li>
                    </ul>
                    <h3><span style="color: #fcb53b;">Document Risk Management Plan</span></h3>
                    <p>In making decisions regarding home visits, all factors need to be considered, including mitigating
                        circumstances such as physical disability, presence of supportive relatives, etc.</p>
                    <p>Where risk is assessed as medium/significant or high/extreme, Banksia Care staff should never visit a
                        residence until they have discussed the risk with their supervisor and a management plan is
                        developed.</p>
                    <p>Management plans must always contain risk minimisation strategies.</p>
                    <p>Risk management strategies need to be built into the individual’s care plan, and the effectiveness
                        monitored and evaluated:</p>
                    <ul>
                        <li>individually between the client and their care coordinator</li>
                        <li>through regular team meetings and care conferences (where applicable). The assessed level of
                            risk will inform risk management options. For example:</li>
                    </ul>
                    <ol>
                        <li style="list-style-type: none;">
                            <ol>
                                <li>The risk level is acceptable and can be managed with existing procedures (no plan
                                    required). For example, a telephone call to the client to ensure that:
                                    <ul>
                                        <li>the worker can safely provide the service</li>
                                        <li>safe and well-lit access is available through gates, doors, and when entering
                                            high-density housing</li>
                                        <li>pets are suitably restrained</li>
                                        <li>the house and entry are illuminated if the visit is to be conducted at night.
                                            (N.B. Avoid visits after dark as much as practicable).</li>
                                    </ul>
                                </li>
                                <li>The level of risk is acceptable but requires adjustments to human resources (e.g., must
                                    be visited by two people) and a risk management plan developed. Two staff should visit
                                    when:
                                    <ul>
                                        <li>the risk of aggression is ‘medium/significant’, or has not been assessed</li>
                                        <li>violence has occurred in the past but not in the recent past, and there are
                                            concerns that the client may be unwell or becoming unwell</li>
                                        <li>reports have indicated that the client is ‘upset’ or ‘distressed’</li>
                                        <li>the staff member visiting:
                                            <ul>
                                                <li>does not know the client</li>
                                                <li>is not trained in assessing aggression or assessing the environment</li>
                                                <li>is not trained in de-escalation or self-defence strategies</li>
                                                <li>is not aware of safe home visiting policies and procedures.</li>
                                            </ul>
                                        </li>
                                        <li>If two workers cannot undertake the home visit, the home visit is not to be
                                            actioned.</li>
                                    </ul>
                                </li>
                                <li>The level of risk is too high and cannot be mitigated through the rearrangement of
                                    resources or a risk management plan.
                                    <ul>
                                        <li>Staff should not visit a client at home if:
                                            <ul>
                                                <li>the assessed risk of aggression is ‘high’ or ‘extreme’</li>
                                                <li>violence is known to have recently occurred, and the perpetrator (client
                                                    or another person) is at the address or could return to or arrive during
                                                    the visit</li>
                                                <li>the client or other person/people present exhibits signs of aggression
                                                    and/or intoxicated by alcohol and/or drugs</li>
                                            </ul>
                                        </li>
                                        <li>If it is considered unsafe to visit a client at home, then alternative
                                            arrangements must be made. These may include:
                                            <ul>
                                                <li>client attendance at a health care facility during office hours</li>
                                                <li>a multi-party appointment with a community mental health case manager.
                                                </li>
                                            </ul>
                                        </li>
                                        <li>When a home visit has been assessed as unsafe, staff will:
                                            <ul>
                                                <li>explain and document the reasons</li>
                                                <li>work with the client to identify more appropriate options</li>
                                                <li>with the client’s consent, refer appropriately.</li>
                                            </ul>
                                        </li>
                                    </ul>
                                </li>
                            </ol>
                            <h3><span style="color: #fcb53b;">Attending the Home Visit</span></h3>
                            <ol>
                                <li>Client home visits on the way to work or on the way home from work:<br>
                                    Home or community visits on the way to work or on the way home from work will not occur
                                    without prior approval of a manager and documented justification for this decision, and
                                    procedures should be put in place to manage such visits.</li>
                            </ol>
                            <ol start="2">
                                <li>Staff movements:<br>
                                    A staff member about to make a home visit must contact their supervisor or, if
                                    unavailable, contact a team member prior to a home visit. Otherwise, the home visit must
                                    not be actioned as employee safety is paramount.<p></p>
                                    <ul>
                                        <li>Prior to leaving for a home visit, the staff member must log the details of
                                            their visit/s into our Hub Staff system:
                                            <ul>
                                                <li>expected time of arrival at the nominated venue or venues if conducting
                                                    several visits or appointments</li>
                                                <li>expected length of each visit or appointment</li>
                                                <li>name, address and telephone number of the client/s being visited</li>
                                                <li>any other appointments that the staff will be conducting while out and
                                                    the order in which visitations are expected to occur</li>
                                                <li>mobile telephone number</li>
                                                <li>a description of the vehicle to be used, including colour, registration
                                                    and type/model.</li>
                                            </ul>
                                        </li>
                                        <li>The staff member visiting a client/s must:
                                            <ul>
                                                <li>review the client’s risk profile</li>
                                                <li>agree on a code word to use if in distress and log it.</li>
                                                <li>The staff member must report to the office by phone upon return and in
                                                    circumstances where the visit has exceeded the expected timeframe.</li>
                                            </ul>
                                        </li>
                                        <li>If a staff member receives a call from a worker at risk:
                                            <ul>
                                                <li>check the address, ask about the nature of the crisis and ask what is
                                                    needed</li>
                                                <li>if you hear the organisational code word, call emergency on 000.</li>
                                            </ul>
                                        </li>
                                        <li>Carry the necessary equipment:<br>
                                            When conducting a home visit, staff should:<p></p>
                                            <ul>
                                                <li>always carry a working mobile phone that is charged</li>
                                                <li>have 000 and the relevant office numbers programmed into the speed dial
                                                    function of the mobile phone</li>
                                                <li>have access to relevant safety equipment such as torches (staff must
                                                    always carry a torch if visiting at night), first aid kits and emergency
                                                    information folders, and that these items are in good working order and
                                                    up to date.</li>
                                            </ul>
                                        </li>
                                        <li>Arrival at a client home visit:
                                            <ul>
                                                <li>Park on the street in a well-lit place where you can’t be parked in or
                                                    obstructed.</li>
                                                <li>Do not enter the home if you can hear people arguing at the premises, if
                                                    you see people using alcohol or drugs at the premises or if you feel
                                                    threatened.</li>
                                                <li>If you become concerned for your safety or are threatened with physical
                                                    harm upon arrival for a client visit, you should:</li>
                                                <li>leave immediately</li>
                                                <li>drive to a safe location and then contact the office, or if urgent, the
                                                    police and then your manager</li>
                                                <li>if necessary, proceed to the nearest police station.</li>
                                            </ul>
                                        </li>
                                        <li>During a home visit:<br>
                                            All staff should receive full training in identifying and managing risks, such
                                            as the risk of aggression and hazards around the home. The following safety
                                            procedures should be adhered to by all staff during home visits.<p></p>
                                            <ul>
                                                <li>Be cautious when entering a client’s home.</li>
                                                <li>If an unfamiliar person opens the door, make sure the client is home and
                                                    that you feel safe before entering. (It may be necessary to abandon the
                                                    visit if you have any concerns).</li>
                                                <li>Ask whether there are other people at home. Be aware of the presence of
                                                    others.</li>
                                                <li>Be aware of house layout and your exit routes.</li>
                                                <li>Keep your keys and mobile phone on you.</li>
                                                <li>Conduct a quick assessment by asking yourself:
                                                    <ul>
                                                        <li>Is the client coherent?</li>
                                                        <li>Is there anything different in their demeanour?</li>
                                                        <li>Are they exhibiting signs of agitation or aggression?</li>
                                                        <li>Is there any evidence of alcohol or drug use?</li>
                                                        <li>Are other people present?</li>
                                                        <li>Is anyone arguing?</li>
                                                        <li>Are there any weapons?</li>
                                                        <li>Is the home in disarray or different than usual?</li>
                                                        <li>NEVER attempt to physically stop violent behaviour or get
                                                            involved in arguments.</li>
                                                        <li>If there is an identified safety risk due to aggression, leave
                                                            immediately.</li>
                                                        <li>Take note of all possible hazards and report them on your return
                                                            to the office.</li>
                                                        <li>If a medical emergency arises while you are on a home visit,
                                                            call 000 and wait for help.</li>
                                                    </ul>
                                                </li>
                                            </ul>
                                        </li>
                                    </ul>
                                </li>
                            </ol>
                            <h3><span style="color: #fcb53b;">Following a Visit</span></h3>
                            <ul>
                                <li>Report any incident or significant observations to your supervisor or manager. (See
                                    Critical situations below).</li>
                                <li>Document any incidents, concerns or significant observations in the client notes.</li>
                                <li>Report to the office via phone after each visit.</li>
                                <li>Speak to your employer if you need debriefing, counselling or support services.</li>
                            </ul>
                            <h3><span style="color: #fcb53b;">Critical Situations</span></h3>
                            <p>Staff must not enter any client’s home if there is a potentially critical situation. Under
                                these circumstances, staff should leave immediately and alert their manager to determine who
                                needs to be contacted for assistance.</p>
                            <p>In circumstances of impending danger, the staff member should contact the relevant emergency
                                services first and then inform their manager.</p>
                            <p>After an incident:</p>
                            <ul>
                                <li>Ensure there is no longer any danger present. If there is concern for the welfare of the
                                    client or others, immediately contact the police and/or ambulance and your manager.</li>
                                <li>Seek medical treatment for yourself if required.</li>
                                <li>Request debriefing, support and/or ongoing counselling if required.</li>
                                <li>Ensure a critical incident report is completed within 24 hours and assessed by a General
                                    Manager.</li>
                                <li>Facilitate debriefing and support for the client and others involved.</li>
                            </ul>
                            <h3><span style="color: #fcb53b;">Responsibilities</span></h3>
                            <p>Banksia Care Managers will ensure the followings:</p>
                            <ul>
                                <li>Establish and communicate guidelines, policies and procedures for safe home visiting,
                                    with clearly defined employee roles and responsibilities.</li>
                                <li>Provide WH&amp;S orientation and ongoing training in safe work practices for all new
                                    staff.</li>
                                <li>Establish effective systems to monitor staff movements and respond as necessary.</li>
                                <li>Establish systems to identify hazards, risk assessment and eliminate or control hazards,
                                    as well as consultative mechanisms, incident reporting and follow-up procedures.</li>
                                <li>Manage violence and aggression in the work environment, with support for injured workers
                                    and return-to-work strategies.</li>
                            </ul>
                            <p>Banksia Care Staff will ensure the following:</p>
                            <ul>
                                <li>Comply with this procedure.</li>
                                <li>Take responsibility for your own and others’ safety.</li>
                                <li>Report all incidents.</li>
                            </ul>

                        </li>
                    </ol>
