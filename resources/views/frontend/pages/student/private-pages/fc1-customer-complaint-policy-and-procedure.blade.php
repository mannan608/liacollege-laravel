                    <h3><span style="color: #fcb53b;">Complaint Management</span></h3>
                    <p>The internal reviewer responsible for managing a complaint will ensure the following:</p>
                    <ul>
                        <li>Register the complaint using the Banksia Care complaint register.</li>
                        <li>Inform the complainant that their concern has been received and provide them with details of the
                            process followed when investigating their complaint.</li>
                        <li>Ensure complaints are examined within ten business days of the complaint being filed.</li>
                        <li>Issue a letter to the complainant within 15 business days outlining what is being done to
                            investigate and resolve the issue, as well as the time frame anticipated for a full resolution.
                        </li>
                        <li>Advise the resolution of the complaint will be within 20 business days of the first lodgment of
                            the complaint.</li>
                        <li>Facilitate queries regarding the outcome of the complaint.</li>
                        <li>Maintain detailed notes of the complainant and complaint, dates, action, resolution and the
                            complainant’s response.</li>
                        <li>Keep copies of all correspondence relating to the complaint on file for seven years.</li>
