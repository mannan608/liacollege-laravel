                    <h2><span style="color: #da591f;">Continuous Improvement Procedure</span></h2>
                    <ul></ul>
                    <h3><span style="color: #fcb53b;">Management will ensure the following:</span></h3>
                    <ul>
                        <li>Remind staff regularly about continuous improvement expectations, as well as the whole team’s
                            responsibilities in identifying and helping facilitate any need for change.</li>
                        <li>Maintain a register for staff to complete when noting any continuous improvement suggestions.
                        </li>
                        <li>They will thoroughly investigate, consider and act on any information gathered and presented
                            both formally and informally.</li>
                        <li>Design and implement a procedure for customer/stakeholder feedback as a mechanism to identify
                            continuous improvement requirements.</li>
                        <li>Maintain records of identified risk assessments to facilitate continuous improvement.</li>
                        <li>Define and incorporate a continuous improvement cycle into its daily operations to demonstrate
                            how the process of continuous improvement is ongoing and not static.</li>
                    </ul>
                    <ul></ul>
                    <h3><span style="color: #fcb53b;">Staff will ensure the following:</span></h3>
                    <ul>
                        <li>Understand that continuous improvement is a whole team responsibility and not a task/job
                            assigned to one individual or team.</li>
                        <li>Acknowledge continuous improvement is a cycle and, therefore, ongoing.</li>
                        <li>Focus on supporting and understanding the needs of Banksia Care’s customers and strive for
                            customer satisfaction.</li>
                        <li>Complete the continuous improvement register for any aspect of the business that identifies a
                            potential improvement is required.</li>
                        <li>Benchmark operational processes and regularly measure performance.</li>
                        <li>Ensure communication with peers and management remains open and valued culture.</li>
                        <li>Focus on effective time management.</li>
                        <li>Identify teamwork as an effective method of achieving desired outcomes.</li>
                        <li>Maintain a culture that appreciates continuous improvement is a routine process of ongoing
                            change and positive growth.</li>
                        <li>Strive to maintain a reputation at Banksia Care that is one of reliability, quality and
                            innovation.</li>
                        <li>Use customer surveys and feedback to improve service, quality of product and a driver for
                            improvement.</li>
                    </ul>
