                    <h2><span style="color: #da591f;">Infection Control Procedure</span></h2>
                    <ul></ul>
                    <h3><span style="color: #fcb53b;">Banksia Care will:</span></h3>
                    <ul>
                        <li>provide adequate and appropriate resources and facilities for handwashing such as clean water,
                            sensor taps, soap, paper towel and sanitiser</li>
                        <li>provide adequate and appropriate PPE and cleaning equipment</li>
                        <li>meet the services duty of care and occupational health and safety obligations to minimise risk
                            of spread of infection</li>
                        <li>ensure ‘how to wash your hands’ posters are displayed near every hand washing station.</li>
                    </ul>
                    <h3><span style="color: #fcb53b;">Hand Care and Hygiene</span></h3>
                    <p>Handwashing must be used on the following occasions in non-clinical environments.</p>
                    <h4><span style="color: #b67318;">Before:</span></h4>
                    <ul>
                        <li>handling raw food</li>
                        <li>providing food to a client</li>
                        <li>providing or assisting with any treatment for a client.</li>
                    </ul>
                    <h4><span style="color: #b67318;">After:</span></h4>
                    <ul>
                        <li>coughing, sneezing or blowing your nose</li>
                        <li>eating or drinking</li>
                        <li>touching your hair, scalp or any wound</li>
                        <li>using the toilet</li>
                        <li>handling urine or faeces</li>
                        <li>handling raw food</li>
                        <li>handling dirty clothing</li>
                        <li>smoking</li>
                        <li>a body substance exposure risk</li>
                        <li>touching a client</li>
                        <li>touching a client’s belongings, surroundings or clothing</li>
                        <li>removing gloves.</li>
                    </ul>
                    <h4><span style="color: #b67318;">During the COVID-19 pandemic, handwashing must include the following
                            occasions:</span></h4>
                    <ul>
                        <li>before touching surfaces or equipment</li>
                        <li>before touching other people</li>
                        <li>before touching your face</li>
                        <li>before eating</li>
                        <li>after touching equipment, products, money, EFTPOS machines, or other surfaces</li>
                        <li>after touching other people</li>
                        <li>after touching your face</li>
                        <li>after coughing or sneezing</li>
                        <li>after eating</li>
                        <li>after coming home after being in a public area or at work.</li>
                    </ul>
                    <h3><span style="color: #fcb53b;">Wound Care</span></h3>
                    <p>Cuts, scrapes and grazing of the skin should be dressed and then covered with a plastic substance
                        should it come into contact with people, food, food preparation equipment or surfaces.</p>
                    <ul></ul>
                    <h3><span style="color: #fcb53b;">Jewellery and Watches</span></h3>
                    <p>These should be removed before work that requires direct client contact. Jewellery and watches should
                        always be removed when using gloves. During the COVID-19 pandemic, these items should not be worn to
                        work or in public environments. If an individual chooses to wear items, they must be sanitised
                        frequently.</p>
                    <ul></ul>
                    <h3><span style="color: #fcb53b;">Nail Hygiene</span></h3>
                    <p>Nails should be kept clean and neatly trimmed. Nail polish must not be flaking as it causes a risk to
                        patient health.</p>
                    <ul></ul>
                    <h2><span style="color: #da591f;">Hand Hygiene&nbsp;Procedure</span></h2>
                    <p>Banksia Care will adhere to the National Hand Hygiene Initiative (NHNI) ‘5 moments for hand hygiene’
                        to minimise the incidence of infectious diseases.</p>
                    <ul></ul>
                    <h3><span style="color: #fcb53b;">When to Perform Hand Hygiene</span></h3>
                    <ul>
                        <li>At all times identified in the Hand Care and Hygiene policy above.</li>
                        <li>The ‘5 moments of hand hygiene’:</li>
                    </ul>
                    <ol>
                        <li>Before touching a resident (this includes all personal care activities).</li>
                        <li>Before performing a procedure.</li>
                        <li>After performing a procedure or a body fluid exposure risk.</li>
                        <li>After touching a resident.</li>
                        <li>After touching resident’s surroundings.</li>
                    </ol>
                    <h3><span style="color: #fcb53b;">How to Perform Hand Hygiene</span></h3>
                    <p>Hands can be cleaned by washing them with soap and warm water or by using alcohol based hand
                        sanitiser. If your hands a visibly dirty you must wash them with soap and water.</p>
                    <ul></ul>
                    <h3><span style="color: #fcb53b;">Hand Washing</span></h3>
                    <ol>
                        <li>Remove rings, watches and ensure your clothing is above your wrist.</li>
                        <li>Turn on warm water.</li>
                        <li>Wet&nbsp;your hands&nbsp;with running water.</li>
                        <li>Apply one-two pumps liquid soap.</li>
                        <li>Rub&nbsp;soap all over the front and back of your hands, over and between your fingers and
                            fingernails, and around your wrists making sure all surfaces have been covered. Do this for at
                            least 20 seconds.</li>
                        <li>Rinse&nbsp;your hands&nbsp;under running water for at least 10 seconds.</li>
                        <li>Turn off&nbsp;the&nbsp;water using&nbsp;the&nbsp;paper towel or your elbow instead&nbsp;of
                            your&nbsp;bare&nbsp;hand.</li>
                        <li>Dry&nbsp;your hands&nbsp;with&nbsp;a clean, disposable paper towel.</li>
                    </ol>
                    <h3><span style="color: #fcb53b;">Hand Sanitiser</span></h3>
                    <ol>
                        <li>Remove rings, watches and ensure your clothing is above your wrist.</li>
                        <li>Apply one-two pumps of alcohol-based hand sanitiser to your cupped hands.</li>
                        <li>Rub all over the front and back of your hands, over and between your fingers and fingernails,
                            and around your wrists making sure all surfaces have been covered.</li>
                        <li>Continue to rub your hands together until they are dry.</li>
                    </ol>
                    <h3><span style="color: #fcb53b;">Cleaning of Blood and Other Bodily Fluids Procedure</span></h3>
                    <p>If blood or other bodily fluids is spilt on a surface the following procedure must be followed.</p>
                    <ol>
                        <li>Put on gloves and other relevant PPE.</li>
                        <li>If there is a risk of splashing wear apron and eye protection.</li>
                        <li>Remove as much of the spill as possible with paper towel and place towel in plastic bag.</li>
                        <li>Use a disposable cleaning cloth or paper towel to wash the area with warm water and detergent
                            and place cloth/towel in plastic bag.</li>
                        <li>Disinfect the area with a solution of household bleach, diluted to the manufacturer’s
                            instructions.</li>
                        <li>Remove all PPE and add to plastic bag with used towelling and cloth, seal and dispose.</li>
                        <li>Wash hands.</li>
                        <li>Display hazard sign and allow area to dry.</li>
                    </ol>
                    <h3><span style="color: #fcb53b;">In the Case of Contact With Blood or Other Bodily Fluids</span></h3>
                    <p>If blood or other bodily fluids comes into contact with a person, the following procedure must be
                        followed.</p>
                    <ol>
                        <li>Remove contaminated clothing.</li>
                        <li>Wash any contacted skin with warm water and soap.</li>
                        <li>If fluid has entered the persons eyes gently wash eyes whilst open with water.</li>
                        <li>If fluid has entered the persons mouth rinse with water, spitting the water out after each rinse
                            and repeat this several times.</li>
                        <li>Report immediately to supervisor.</li>
                    </ol>
                    <h2><span style="color: #da591f;">Personal Protection Equipment</span></h2>
                    <p>Personal protective equipment (PPE) must be worn when workers find themselves in situations where
                        they may be exposed to infection risk.</p>
                    <ul></ul>
                    <h4><span style="color: #b67318;">Gloves </span></h4>
                    <p>must be worn to protect the hands from coming into contact with infectious substances or other
                        contaminants, including when:</p>
                    <ul>
                        <li>in surgery and medical procedures</li>
                        <li>providing hygiene support to clients</li>
                        <li>providing first aid for cuts and abrasions</li>
                        <li>cleaning up contaminated spills or surfaces that have been contaminated with bodily fluids</li>
                        <li>coming into contact with a person that you know is infected with an infectious disease</li>
                        <li>handling used linens or other materials that have bodily fluids or waste on them.</li>
                    </ul>
                    <h4><span style="color: #b67318;">Face masks </span></h4>
                    <p>must be worn to protect both the mouth and the nose (the respiratory system) from coming into contact
                        with infectious substances or other contaminants, including when:</p>
                    <ul>
                        <li>in surgery and medical procedures</li>
                        <li>interacting with others if you have an infectious disease</li>
                        <li>interacting with an infected person</li>
                        <li>in all public environments during outbreaks and pandemics, being sure to follow government
                            directives.</li>
                    </ul>
                    <h4><span style="color: #b67318;">Aprons and protective gowns</span></h4>
                    <p>must be worn to protect the body and clothing from coming into contact with infectious substances or
                        other contaminants, including when:</p>
                    <ul>
                        <li>taking part in surgical and medical procedures</li>
                        <li>cleaning up spills of bodily fluids, such as blood or urine, that could be transferred to
                            clothing</li>
                        <li>cleaning surfaces or equipment that is affected by liquid.</li>
                    </ul>
                    <h4><span style="color: #b67318;">Glasses</span></h4>
                    <p>must be worn to protect the eyes from coming into contact with infectious substances or other
                        contaminants that could splash up into them, including when:</p>
                    <ul>
                        <li>a part of surgical and medical procedures</li>
                        <li>cleaning up spills of bodily fluids such as blood or urine</li>
                        <li>cleaning surfaces or equipment which have become contaminated with large amounts of bodily
                            fluids which could splash or become aerosolised</li>
                    </ul>
                    <h4><span style="color: #b67318;">Hazard inspections</span></h4>
                    <p>are to be completed regularly to identify any hazards. Stakeholders are also able to report
                        identified hazards and a risk assessment to be conducted to manage the risk.</p>
                    <ul></ul>
                    <h4><span style="color: #b67318;">Risk assessments</span></h4>
                    <p>are conducted following hazard inspections. Risk assessment should include risk, likelihood,
                        consequence, level, and selected risk controls.</p>
                    <ul></ul>
                    <h2><span style="color: #da591f;">Infection Hazards</span></h2>
                    <p>Infection risk controls can include:</p>
                    <ul>
                        <li>using personal protective equipment</li>
                        <li>using hand hygiene strategies, including hand washing and using hand sanitiser</li>
                        <li>using cough etiquette</li>
                        <li>using surface cleaning procedures, including cleaning and disinfecting</li>
                        <li>using equipment cleaning procedures, including cleaning, disinfecting and sterilising</li>
                        <li>handling used linen handling safely KindCare Infection Control Policy and Procedure © Learn For
                            Work</li>
                        <li>disposing of contaminated waste promptly and safely</li>
                        <li>receiving vaccinations if they are available.</li>
                    </ul>
                    <p>In response to the COVID-19 pandemic, the following additional precautions may be required:</p>
                    <ul>
                        <li>restricting visitor access to the Banksia Care facility</li>
                        <li>social distance in rules</li>
                        <li>self-isolate of residents</li>
                        <li>reducing physical contact with surfaces and objects.</li>
                    </ul>
                    <h2><span style="color: #da591f;">Environmental Cleaning</span></h2>
                    <p>The surfaces people regularly come into contact with in the work environment must be cleaned
                        regularly. Workers must:</p>
                    <ul>
                        <li>clean surfaces immediately after they are exposed to any bacterial or infectious contaminant,
                            including any bodily fluids</li>
                        <li>clean surfaces before and after using them to prepare or handle food</li>
                        <li>clean regular contact surfaces at the end of every work day, such as benches and countertops in
                            client’s rooms or homes</li>
                        <li>clean client equipment and self-service machines at the end of every day, such as computers,
                            pens, clipboards and other equipment used in client orientation</li>
                        <li>clean frequently touched parts of the building throughout the day, such as doors, door
                            handles/push plates, handrails, and lift buttons.</li>
                    </ul>
                    <h2><span style="color: #da591f;">Equipment Cleaning</span></h2>
                    <p>Anything that has come into contact with bodily fluids or contaminated food threatens workers’
                        health. As a result, it must be disinfected to prevent the spread of infectious diseases.
                        Contaminated equipment can include:</p>
                    <ul>
                        <li>first aid equipment</li>
                        <li>surgical equipment, such as scalpels, tweezers and forceps</li>
                        <li>equipment used during toileting or bathing</li>
                        <li>personal hygiene products.</li>
                    </ul>

