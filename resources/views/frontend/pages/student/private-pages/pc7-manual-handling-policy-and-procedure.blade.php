                            consultative approach when identifying, assessing and controlling the risks involved with manual
                            handling in the workplace.</li>
                        <li>Ensure that all employees receive appropriate manual handling training for their workplace.</li>
                        <li>Establish and monitor key performance indicators measuring the effectiveness of the manual
                            handling program.</li>
