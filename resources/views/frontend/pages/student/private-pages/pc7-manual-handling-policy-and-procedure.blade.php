                    <h3><span style="color: #fcb53b;">Principles of Manual Handling</span></h3>
                    <ul>
                        <li>Stand/sit upright, maintaining the normal curve of the spine.</li>
                        <li>Keep feet wide apart.</li>
                        <li>Bend at the hips and knees.</li>
                        <li>Avoid twisting and bending the back.</li>
                        <li>Point feet in the direction of movement.</li>
                        <li>Keep the load close to the body.</li>
                        <li>Ensure a firm secure grip.</li>
                        <li>Tighten core.</li>
                        <li>Use the legs.</li>
                        <li>Push rather than pull.</li>
                        <li>Safe working height at all times.</li>
                    </ul>
                    <h3><span style="color: #fcb53b;">Dealing with Clients with Challenging Behaviours</span></h3>
                    <p>Strategies to gain assistance from clients:</p>
                    <ul>
                        <li>Approach in a calm manner.</li>
                        <li>Introduce yourself (eye level, position yourself off-centre, maintain eye contact).</li>
                        <li>Provide as much cueing and explanation as possible (verbal/physical).</li>
                        <li>Provide diversion tactics (discussing interests, family members, etc.).</li>
                        <li>Offer rewards (promising a nice hot cup of tea if assisting in getting out of bed).</li>
                    </ul>
                    <p>Should the above not be successful:</p>
                    <ul>
                        <li>Re-attend at a later time (negotiate if possible).</li>
                        <li>Re-attend with another care staff (someone with good history with the resident, maybe a
                            favourite care staff).</li>
                        <li>Re-attend with alternative care staff.</li>
                    </ul>
                    <h3><span style="color: #fcb53b;">Rolling into Bed</span></h3>
                    <ul>
                        <li>Requires two staff assisting.</li>
                        <li>First assist will place hands at client’s shoulder and hip level, guiding the movement.</li>
                        <li>Second assist places their hands on top of the first assistant’s hands.</li>
                        <li>A lunge position is used to engage the core muscles.</li>
                        <li>Staff roll the client towards the first assistant by pushing away from self.</li>
                    </ul>
                    <h3><span style="color: #fcb53b;">Rolling with a Slide Sheet</span></h3>
                    <ul>
                        <li>Requires two staff assisting.</li>
                        <li>Fold sheet in half or use two slide sheets.</li>
                        <li>Position under the person by rolling them.</li>
                        <li>Folded edge is placed under the person.</li>
                        <li>Open edges of the sheet to face you.</li>
                        <li>Grasp top layer with palms facing upwards.</li>
                        <li>Adopt a squat position, and pull upwards while the other carer assists at the side the resident
                            is rolled.</li>
                        <li>Remove the slide sheet by pulling the bottom layer of the sheet out at one corner.</li>
                    </ul>
                    <h3><span style="color: #fcb53b;">Moving a Client up the Bed</span></h3>
                    <ul>
                        <li>Requires two staff assisting.</li>
                        <li>Ensure the bed is flat.</li>
                        <li>Slide sheet in half or use two slide sheets.</li>
                        <li>Open edges facing head of the bed.</li>
                        <li>Slide sheet placed between shoulder level and hip level.</li>
                        <li>Position self-mirroring partner opposite, one hand at shoulder level, the other hand at hip
                            level.</li>
                        <li>Grasp top layer palm facing upwards.</li>
                        <li>Pull the sheet to make it taught.</li>
                        <li>Feet facing in the direction of the movement – toward bed head, side lunge position, back
                            straight, knees bent, eye contact with partner.</li>
                        <li>Determine when to slide one…two…three…</li>
                        <li>Client can assist – bend legs/chin to chest.</li>
                        <li>Move with lunging movement from the foot nearest the foot end of bed, to the foot nearest to the
                            head end of the bed.</li>
                        <li>Remove the sheet by rolling resident.</li>
                    </ul>
                    <h3><span style="color: #fcb53b;">Assisting with Lying to Sitting</span></h3>
                    <p>1 x assist – verbal/physical cueing</p>
                    <ul>
                        <li>Stand on the side of the bed where the resident will get out of the bed.</li>
                        <li>Ask resident to bridge towards the side where they will be getting up from.</li>
                        <li>Ask resident to roll onto their side (if possible).</li>
                        <li>Raise the bed head up to a suitable height, approx. 30-50 degrees.</li>
                        <li>Ask them to push up with one arm while digging up from the mattress with the other arm while
                            lowering their legs simultaneously to assist with the manoeuvre.</li>
                    </ul>
                    <p>1 x assist – physical assistance</p>
                    <ul>
                        <li>Repeat as above; however, some physical assistance at the upper trunk or lower limbs is required
                            throughout the whole process (bridging, rolling, pushing up, and lowering legs).</li>
                        <li>Ensure correct posture is adopted, abdominal bracing, and hips and knees are bent.</li>
                        <li>Raising bed to a higher level may help (if tolerated by resident), and ensure bed is then
                            lowered.</li>
                    </ul>
                    <p>2 x assists – physical assistance</p>
                    <ul>
                        <li>Both staff to stand on the side that the client will get out of the bed.</li>
                        <li>Repeat as above; however, physical assistance will be required at both the upper trunk and lower
                            trunk for all steps (bridging, rolling, pushing up, and lowering legs).</li>
                        <li>Ensure correct posture is adopted, abdominal bracing, and hips and knees are bent.</li>
                        <li>Raising bed to a higher level may help (if tolerated by client), and ensure bed is then lowered.
                        </li>
                        <li>Should any of the steps cause potential strain to resident or yourself, refer to
                            nursing/physiotherapy staff where the resident can be reviewed</li>
                    </ul>
                    <h3><span style="color: #fcb53b;">Standing lifters</span></h3>
                    <ul>
                        <li>Weight-bearing capability.</li>
                        <li>Able to lift their feet onto the footplate.</li>
                        <li>Must have 90 degrees of shoulder elevation.</li>
                        <li>Grip handles with both hands.</li>
                        <li>Predictable and dependable when sitting.</li>
                        <li>Need stand-by or independent sitting balance</li>
                    </ul>
                    <p>2x staff assist at all times</p>
                    <ul>
                        <li>Client sitting up and able to rest feet on footplate and place shins against shin pad.</li>
                        <li>Apply brakes once into position.</li>
                        <li>Waist strap positioned appropriately.</li>
                        <li>Lower the lifter arms and assist client to place their arms on the outside of the sling.</li>
                        <li>Place cord onto hooks so they are the same length.</li>
                        <li>Do not raise resident too high, as this will cause pressure under their arms.</li>
                        <li>Release brakes and move resident to their new position.</li>
                        <li>When client is located to new position apply brakes and loosen sling cords and remove sling.
                        </li>
                    </ul>
                    <h3><span style="color: #fcb53b;">Standing lifters</span></h3>
                    <p>Sling and Hoist lifters</p>
                    <p>Dependent clients – chair or bedfast residents.</p>
                    <p>2x assists are required at all times</p>
                    <ul>
                        <li>Position the client in the sling by rolling them side to side or sitting the resident forward
                            with the aid of electric bed.</li>
                        <li>Adjust the loop fittings -–sitting or lying.</li>
                        <li>Raise slightly off the bed – ensure they are comfortable before proceeding further.</li>
                        <li>When off the bed move the lifter.</li>
                        <li>One staff holds onto the client and ensures they are steady and safe.</li>
                        <li>Other staff maneuvers the lifter.</li>
                    </ul>
                    <h3><span style="color: #fcb53b;">Assisting with Sit-to-Stand</span></h3>
                    <p>Verbal Cues Only</p>
                    <ul>
                        <li>Feet behind knees, shoulder width apart.</li>
                        <li>Lean forward, having nose over toes.</li>
                        <li>Push up using upper limbs, using bed or arms of chair.</li>
                        <li>Do not allow client to pull on the aid.</li>
                        <li>Aid may need to stabilise once resident is upright.</li>
                    </ul>
                    <p>Providing 1 or 2 x Physical Assists</p>
                    <ul>
                        <li>Stand side on, your front foot in front of their foot to stop slips.</li>
                        <li>Front knee can also be used to block their knee.</li>
                        <li>Place cupped hand on client’s shoulder.</li>
                        <li>Rock client if needed to assist with bringing their ‘nose over toes’ and aid momentum.</li>
                    </ul>
                    <h3><span style="color: #fcb53b;">Walking Aids</span></h3>
                    <p>Walking stick or Quad stick</p>
                    <ul>
                        <li>Guide client to hold walking stick or quad stick on their strong side.</li>
                        <li>Ensure client stands on their weaker side.</li>
                    </ul>
                    <p>Static or Rolling Frame Walkers</p>
                    <ul>
                        <li>Check the walker level is correct for the client.</li>
                        <li>Ensure the hand brakes are functional on rolling walker.</li>
                        <li>Guide client with one hand at the back of the person holding onto either their trousers or
                            supporting the waist.</li>
                        <li>Use the other hand to steady and move the frame if necessary.</li>
                        <li>Check aids regularly.</li>
                    </ul>
                    <h3><span style="color: #fcb53b;">Transfer to car</span></h3>
                    <ul>
                        <li>Assess the situation: Before attempting to move the person, assess their condition and ask if
                            they need special assistance or if any factors could make the transfer more difficult.</li>
                        <li>Position the car: Park the car on a level surface with enough room on either side for the person
                            and any mobility aids, such as a wheelchair or walker.</li>
                        <li>Open the car door: Open the car door wide enough to allow the person to enter comfortably.</li>
                        <li>Adjust the seat: Adjust the car seat to its highest position and move it as far back as possible
                            to give them more room to manoeuvre.</li>
                        <li>Open the card door window: The window ledge may be used to help support the person while
                            standing.</li>
                        <li>Position the mobility aid: If the person uses a mobility aid, such as a wheelchair or walker,
                            position it as close as possible to the car, making sure it is securely locked and stable.</li>
                        <li>Position your body so that your backside is against the inside of the door to stop it from
                            swinging shut.</li>
                        <li>Provide support: If the person is able to stand and walk with assistance, provide support by
                            holding their arm or waist as they move towards the car. Support them as they turn around so the
                            backs of their legs are touching the car seat.</li>
                        <li>Help them to gently lower themselves onto the seat. Protect their head while they do this by
                            holding your hand above it.</li>
                        <li>If they need more support, use a transfer belt or other assistive device to help them stand and
                            move. Remember to communicate clearly with the client .<em>’On the count of three, you are going
                                to sit. One, two, three, sit.’</em></li>
                        <li>Encourange the person to swing and lift their legs into the car. If they need assistance to do
                            this, lift one leg at a time making sure to bend your knees.</li>
                        <li>Assist with putting seat belt on.</li>
                        <li>Assist with closing the car door.</li>
                    </ul>
                    <h3><span style="color: #fcb53b;">Transfer from car</span></h3>
                    <ul>
                        <li>Assess the situation: Before attempting to move the person, assess their condition and ask if
                            they need special assistance or if any factors could make the transfer more difficult.</li>
                        <li>Position the car: Park the car on a level surface with enough room on either side for the person
                            and any mobility aids, such as a wheelchair or walker.</li>
                        <li>Adjust the seat: If the person is able to move, adjust the car seat to its highest position and
                            move it as far back as possible to give them more room to maneuver.</li>
                        <li>Position the mobility aid: If the person uses a mobility aid, such as a wheelchair or walker,
                            position it next to the car, making sure it is securely locked and stable.</li>
                        <li>Open the car door: Open the car door wide enough to allow the person to exit comfortably.</li>
                        <li>Provide support: If the person is able to stand and walk with assistance, provide support by
                            holding their arm or waist as they move out of the car. If they need more support, use a
                            transfer belt or other assistive device to help them stand and move. Remember to communicate
                            clearly with the client .<em>“On the count of three, you are going to stand. One, two, three,
                                stand.”</em></li>
                        <li>Help them sit: If the person needs to sit before moving to their mobility aid, position a sturdy
                            chair next to the car and help them sit down.</li>
                        <li>Move to mobility aid: Once the person is ready, assist them in moving to their mobility aid,
                            making sure to position it as close as possible to the car.</li>
                        <li>Transfer to mobility aid: Depending on the person’s needs, transfer them from the car to their
                            mobility aid using a transfer board or other assistive device. Make sure the transfer is smooth
                            and gentle to avoid causing any discomfort or injury.</li>
                        <li>Secure mobility aid: Once the person is safely in their mobility aid, make sure it is securely
                            locked and stable before moving them away from the car.</li>
                        <li>Assist with seat belt: If the person is getting back in the car, assist them in securing their
                            seat belt before closing the car door.</li>
                    </ul>
