                        <li>All clients are assessed as to their physical capabilities and manual handling needs.</li>
                        <li>Any change from time to time staff are frequently revised and updated.</li>
                        <li>There is familiarity and compliancy by staff to a resident’s Manual Handling Care Plan to ensure
                            client’s and staff safety.</li>
                    </ul>
                    <h3><span style="color: #fcb53b;">Executive team will ensure the following:</span></h3>
                    <ul>
                        <li>Ensure the policy’s effective implementation and hold supervisors and managers accountable for
                            their specific responsibilities.</li>
                        <li>Allocate adequate resources to support the policy by working towards re-design, modifying and
                            maintaining buildings, equipment, furniture, and/or purchasing equipment suitable for the
                            application of reduced-risk work practices.</li>
