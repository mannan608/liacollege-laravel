                    <h2><span style="color: #da591f;">Background</span></h2>
                    <p>In Australia, designated professionals are required to report suspected “abuse and neglect” cases to
                        government authorities.</p>
                    <p>An abuser can be physically abused, sexually abused, affected emotionally or psychologically, or even
                        financially abused.</p>
                    <p>The term “neglect” refers to the deprivation of sustenance, shelter, medical care, and financial
                        support.</p>
                    <p>There are mandatory reporting laws in all Australian states and territories. These laws, however,
                        vary from jurisdiction to jurisdiction. Professionals are required to report, as well as the types
                        of abuse and neglect that must be reported.</p>
                    <p>Reporting suspicions of physical abuse, sexual abuse, emotional abuse and neglect is mandatory in
                        most jurisdictions.</p>
                    <p>The legislation in all jurisdictions protects the reporter’s identity. As long as their report is
                        made in good faith, the reporter cannot be held liable in any civil, criminal, or administrative
                        proceeding.</p>
