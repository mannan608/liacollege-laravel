                    <h2><span style="color: #da591f;">Mandatory Reporting Procedure</span></h2>
                    <ul></ul>
                    <h3><span style="color: #fcb53b;">Indicators of Abuse</span></h3>
                    <p>A number of indicators indicate that someone has been abused or neglected. A single indicator, or a
                        combination of indicators, does not prove abuse or neglect is occurring. It is important for staff
                        to be alerted to abuse and neglect when repeated occurrences or several indicators occur.</p>
                    <p>Here are a few indicators:</p>
                    <ul>
                        <li>Withdrawal or reduced participation</li>
                        <li>Risk-taking behaviour</li>
                        <li>Self-mutilation</li>
                        <li>Unexplained injury or bruising</li>
                        <li>Agitation</li>
                        <li>Absence</li>
                    </ul>
                    <p>Staff must notify their manager immediately whenever a client requires protection from suspected
                        abuse.</p>
                    <ul></ul>
                    <h2><span style="color: #da591f;">Reporting Procedure</span></h2>
                    <p>Staff must notify their manager immediately whenever a client requires protection from suspected
                        abuse.</p>
                    <ul></ul>
                    <h3><span style="color: #fcb53b;">1. Document and report the incident</span></h3>
                    <p>Injuries or behaviour of clients should be described and dated.</p>
                    <p>Include your own observations, client X’s statement, or witness Y’s statement as the source of
                        concerns. Take note of the action taken.</p>
                    <p>Reporting to the supervisor, removing the individual from harm (where safe and appropriate),
                        discussing the matter with senior peers, or reporting to the appropriate regulatory agency are some
                        of the options.</p>
                    <ul></ul>
                    <h3><span style="color: #fcb53b;">2. Prepare the report</span></h3>
                    <p>Describe the date(s), the referring organisation, the reporter’s name and involvement, and the
                        reasons for the concern.</p>
                    <ul></ul>
                    <h3><span style="color: #fcb53b;">3. File the report</span></h3>
                    <p>File according to legislative guidelines and Banksia Care’s Record-Keeping Policy and Procedure. The
                        police should be contacted if criminal charges are suspected. Discuss this with your immediate
                        manager.</p>

