                    <h2><span style="color: #da591f;">Administration of Medication Procedure</span></h2>
                    <ul></ul>
                    <h3><span style="color: #fcb53b;">Management will ensure the following:</span></h3>
                    <ul>
                        <li>Regularly consult with the team wherever practical on all medication administration issues and
                            regulatory or workplace changes that may affect their workplace.</li>
                        <li>Ensure Banksia Care complies with all State guidelines and regulatory requirements in relation
                            to the administration of medication.</li>
                        <li>Attend any professional training or leadership workshops related to medication management to
                            ensure staff are aware and informed of industry changes and trends.</li>
                        <li>Investigate incidents to prevent any reoccurrence and implement the change required.</li>
                        <li>Support staff by providing wellness support staff to ensure mental health is supported as well
                            as physical, especially in dealing with chronically ill, severely disabled, aggressive or
                            palliative care residents.</li>
                        <li>Provide facilities, training, storage and systems that support staff and health practitioners to
                            use medication wisely and to minimise risk and error.</li>
                        <li>Work in partnership with medical professionals to ensure the needs of residents are best met in
                            terms of medication and health care.</li>
                        <li>As the Approved Provider, and in accordance with the Drugs, Poisons and Controlled Substances
                            Act 1981, ensure a Registered Nurse manages the administration of medication to residents
                            receiving high-level residential care.</li>
                        <li>In the first instance, rely on previous doctor’s notes and instructions for medication taken,
                            but will request a review from the treating practitioner moving forward (in consultation with
                            the resident and carer/ representative).</li>
                        <li>Ensure registered nurses consult with the relevant doctor to formalise and approve the new
                            medication order before the expiration of any existing order.</li>
                        <li>Ensure staff, residents and carers/families are familiar with the Administration of Medication
                            Policy.</li>
                        <li>Ensure each facility has a medication administration log that meets regulatory requirements.
                        </li>
                        <li>Conduct regular reviews of completed medical forms to ensure they are being completed correctly.
                        </li>
                        <li>File and store medication authority forms and logs confidentially.</li>
                        <li>Keep medication authority forms and logs for the prescribed length of time.</li>
                    </ul>
                    <h3><span style="color: #fcb53b;">Residents who manage own medication regime will ensure the
                            following:</span></h3>
                    <ul>
                        <li>Be provided with safe and secure storage of their medications.</li>
                        <li>Have their capability to self administer assessed and reviewed regularly by a medical
                            practitioner.</li>
                        <li>Have the approval of self administration noted on their file.</li>
                        <li>Understand that a nursing staff member will remain with them until the medication is swallowed.
                        </li>
                        <li>Ensure they don’t crush any enteric coated drug. If swallowing is difficult, a liquid alternate
                            can be sought.</li>
                        <li>Never share or offer paracetamol, antacids or any drug or medical substance to any other
                            resident.</li>
                    </ul>
                    <h3><span style="color: #fcb53b;">Staff authorised to administer medication (Residential Facility) will
                            ensure the following:</span></h3>
                    <ul>
                        <li>Include medical practitioners, registered nurses, *enrolled nurses and *personal care/support
                            workers.</li>
                        <li>Acknowledge that *enrolled nurses (who do not hold a qualification in medicine administration
                            approved by the Nursing and Midwifery Board of Australia) and personal care/support workers
                            (with appropriate medicine administration training) may, in some circumstances, be competent to
                            administer medicine under the delegation of a registered nurse. Delegation must be recorded in
                            the client’s Individual Care Plan under the Medication field.</li>
                        <li>All PRN (as needed) medications must be administered by a Registered Nurse. EN and personal care
                            workers may only administer if the RN is absent.</li>
                        <li>Consult a medical practitioner if the following medications are needed to to be given more than
                            once:
                            <ul>
                                <li>Paracetamol (for pain and discomfort – aches)</li>
                                <li>Mylanta (for indigestion)</li>
                                <li>Preparations for constipation</li>
                            </ul>
                        </li>
                        <li>Be trained in the Administration of Medication Policy.</li>
                        <li>Be aware of and take note accordingly of any medic alert bracelet, jewellery, tattoo or device
                            worn or displayed by the resident.</li>
                        <li>Ensure all medication trolleys, medication fridges and medical/first aid cabinets are locked
                            when not in use.</li>
                        <li>Check medication expiry dates prior to administering.</li>
                        <li>Consult a medical practitioner and record on medication log if a resident refuses to take any
                            scheduled medication.</li>
                        <li>In the event medication is administered incorrectly, report to the registered nurse or
                            management team immediately, who will report to the regulatory authority within 24 hours.</li>
                        <li>Inform the resident’s doctor if an existing medication is running low.</li>
                        <li>Ensure they read, understand and follow any anaphylaxis or asthma management plan in place for
                            any resident requiring nebulisers, puffers, or EpiPens.</li>
                        <li>Ensure when administering medication that two staff witness at all times. Staff will be
                            responsible for:
                            <ul>
                                <li>checking a medication authority form has been authorised by the resident, next of kin
                                </li>
                                <li>checking the medication authority form for complete details of the resident, name of the
                                    required medication, dose, time and date and the way in which the medication is
                                    administered</li>
                                <li>checking that all information on the medication authority form is in line with the
                                    instructions labelled on the medication.</li>
                            </ul>
                        </li>
                        <li>Wash hands, measure the medication according to the medication authority form, administer
                            medication as required, and complete and fill out the medication administration log with details
                            of – the name of medication, time, date, and dose.</li>
                        <li>Require a second staff member to witness and initial as a witness. Medication cannot be
                            administered without a second authorised person.</li>
                        <li>Return medication back to the safe stored area.</li>
                        <li>Where a resident is discharged from hospital or a day procedure, ensure medication is supplied
                            on a hospital medication chart (Schedule 4 or Schedule 8 poison).</li>
                    </ul>
