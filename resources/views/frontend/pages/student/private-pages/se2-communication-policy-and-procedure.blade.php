                        principles of our <em>Communication Policy</em>.</p>
                    <h3><span style="color: #fcb53b;">Communication Channels</span></h3>
                    <p>Banksia Care has several communication channels for intended purposes, including the following:</p>
                    <ul>
                        <li>Email: Used for messages to staff, clients, and other stakeholders.</li>
                        <li>Client surveys: Used to gather information and feedback from clients for a range of purposes.
                        </li>
                        <li>Presentation: Used to present information verbally for internal work groups such as meetings,
                            inductions, or to external clients and stakeholders.</li>
                        <li>Business documentation: Used to present information formally in written format to staff members,
                            such as reports, policies and procedures, and other business-related documents. Also used to
                            present information in written format to clients and other stakeholders, such as business
                            correspondence, invoices, and other business-related matters.</li>
                        <li>Cloud-based collaborative tools: Used by work groups of staff to collaborate, share, and
                            communicate on projects online.</li>
                        <li>Website and social media channels: Used to inform clients of services offered and by staff to
                            access policies and procedures.</li>
                        <li>Instant messaging: Used to allow real-time communication between staff members informally.</li>
                        <li>Video-conferencing: Used for video communication between staff, clients, and other stakeholders
                            where communication occurs remotely.</li>
                        <li>Phone: Used for real-time discussion between staff members and clients.</li>
                    </ul>
