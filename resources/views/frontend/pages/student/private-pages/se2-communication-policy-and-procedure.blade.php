                    <h2><span style="color: #da591f;">Communication Procedure</span></h2>
                    <h3><span style="color: #fcb53b;">Meeting Preparation:</span></h3>
                    <p>Prior to a meeting taking place, the presenter should consider the following:</p>
                    <ul>
                        <li>Identify the objective(s) of the meeting.</li>
                        <li>Identify the audience of the meeting.</li>
                        <li>Schedule a time and location for the meeting location.</li>
                        <li>Identify the items to be covered at the meeting to meet its objective(s).</li>
                        <li>Ensure that they have reserved/booked out facilities required for the meeting.</li>
                        <li>Develop a Meeting Agenda and distribute this to the audience of the meeting prior to the meeting
                            occurring.</li>
                        <li>Prepare appropriate materials as required for the meeting, such as handouts, equipment such as
                            projectors and computers, catering, or any other materials needed.</li>
                    </ul>
                    <h3><span style="color: #fcb53b;">Meeting Agenda:</span></h3>
                    <ul>
                        <li>The management’s responsibility is to organise formal meetings and delegate meeting preparation
                            tasks to relevant Banksia Care staff members.</li>
                        <li>All information relating to the meeting topic and focus of discussion must be added to the
                            agenda and numbered consecutively as agenda items.</li>
                        <li>Each agenda item must be allocated appropriate timing and ensure specific timeframes are
                            provided, such as welcome and introduction to meeting topic 9:30 a.m. – 9:45 a.m.</li>
                        <li>The meeting agenda document must be saved using the following format: Name of meeting-date of
                            meeting, such as Care Team Meeting-15.01.20XX.</li>
                    </ul>
                    <h3><span style="color: #fcb53b;">Notice to Attendees:</span></h3>
                    <ul>
                        <li>All meeting attendees must be given at least one week’s notice before the meeting.</li>
                        <li>Attendees must receive notification of the meeting via an email with information about:
                            <ul>
                                <li>the type of meeting being planned and its objectives</li>
                                <li>the meeting requirements and any preparation that the attendees may require</li>
                                <li>suitable date and time, and</li>
                                <li>any technical requirements that the attendees may need to prepare in advance.</li>
                            </ul>
                        </li>
                        <li>The meeting agenda must be shared with the attendees via email at least three days prior to the
                            meeting, along with any other relevant documentation attached with the email as well.</li>
                    </ul>
                    <h3><span style="color: #fcb53b;">Meeting Attendance:</span></h3>
                    <ul>
                        <li>All meeting attendees must respond back to the email notification about the meeting within two
                            days of receiving this notification.</li>
                        <li>If any meeting attendees cannot attend the meeting for some reason, they must respond to the
                            email notification immediately stating the reason for their absence.</li>
                        <li>All meeting invitations that have been declined must be noted in apologies in the meeting
                            minutes.</li>
                    </ul>
                    <h3><span style="color: #fcb53b;">Other Resources:</span></h3>
                    <ul>
                        <li>The Banksia Care staff member responsible for making meeting arrangements must identify all
                            resources required for effectively running the meeting, for e.g., technology requirements,
                            internet connectivity, ensuring there is no noise disturbance, proper signs are available to
                            indicate an ongoing meeting, conference phones and speakers, presentation screen and projectors
                            etc.</li>
                        <li>The Banksia Care staff member must ensure that they take approval from the management for using
                            all the extra resources required for organising the meeting effectively, for e.g., equipment
                            required, internet connection availability and any other associated resources and finalise
                            arrangements for the set-up of all resources required for the meeting, at least one day prior to
                            the meeting day.</li>
                    </ul>
                    <h3><span style="color: #fcb53b;">Meeting Delivery:</span></h3>
                    <p>The presenter should conduct the meeting as per the Meeting Agenda by:</p>
                    <ul>
                        <li>providing an introduction to the purpose of the meeting</li>
                        <li>taking attendance</li>
                        <li>covering minutes from any previous meeting</li>
                        <li>covering agenda items</li>
                        <li>coming to an agreement about planned outcomes</li>
                        <li>provide a closing to the meeting, thanking the attendees for their time.</li>
                        <li>The presenter should ensure that they and the attendees show respect to other attendees by:</li>
                        <li>ensuring attendees don’t speak over other attendees</li>
                        <li>ensuring that attendees act ethically as per the organisation’s Code of Ethics during the
                            meeting</li>
                        <li>ensuring attendees answer questions posed to them openly and with honesty</li>
                        <li>managing disagreements between attendees, ensuring that differences of opinions are
                            acknowledged, and that rigorous examination of all options is explored to reach a compromise
                        </li>
                        <li>keeping negotiations focused on key issues and moving forward towards a final resolution.</li>
                    </ul>
                    <h3><span style="color: #fcb53b;">Meeting Finalisation:</span></h3>
                    <p>Once the meeting has concluded, the presenter should complete the following:</p>
                    <ul>
                        <li>Document the meeting.</li>
                        <li>Distribute the meeting minutes to all attendees.</li>
                        <li>Upload the meeting minutes to the organisation’s cloud-based storage system following Banksia
                            Care’s Document Management Policy &amp; Procedures.</li>
                        <li>Follow up with attendees on actions they have been assigned during the meeting to ensure they
                            are completed.</li>
                    </ul>

