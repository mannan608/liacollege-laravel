                    <h2><span style="color: #da591f;">Illness</span></h2>
                    <h3><span style="color: #fcb53b;">Symptoms Indicating Illness in Both Staff and Residents May
                            Include:</span></h3>
                    <ul>
                        <li>behaviour that is unusual</li>
                        <li>high temperature or fevers</li>
                        <li>loose bowels</li>
                        <li>faeces with grey, pale or contains blood</li>
                        <li>vomiting</li>
                        <li>discharge from the eye or ear</li>
                        <li>skin that displays rashes, blisters, spots, crusty or weeping sores</li>
                        <li>loss of appetite</li>
                        <li>dark urine</li>
                        <li>headaches</li>
                        <li>stiff muscles or joint pain</li>
                        <li>continuous scratching of scalp or skin</li>
                        <li>difficult in swallowing or complaining of a sore throat</li>
                        <li>persistent, prolonged or severe coughing</li>
                        <li>difficulty breathing.</li>
                    </ul>
                    <h3><span style="color: #fcb53b;">High Temperatures or Fevers</span></h3>
                    <ul>
                        <li>People get fevers or temperatures for all kinds of reasons. This could range from a mild
                            infection to the flu or even a more deadly infectious disease.</li>
                        <li>Recognised authorities define a normal temperature range between 36.2°C and 37.2°C.</li>
                        <li>A temperature is not considered high until it reaches 38 degrees.</li>
                    </ul>
                    <h3><span style="color: #fcb53b;">Treatment of Temperature:</span></h3>
                    <ul>
                        <li>Always use a properly approved thermometer to take a temperature.</li>
                        <li>When the temperature is 37.5 – 38 degrees, monitor and take the temperature again in 15 minutes.
                        </li>
                        <li>If after 15 minutes the temperature has gone down, continue to take every 15 minutes until the
                            temperature has remained down for 2 hours.</li>
                        <li>If the temperature is 38 or above after 15 minutes, administer the required medication as
                            instructed in the ‘administering medication policy’.</li>
                        <li>Then continue to take the temperature every 15 minutes; if after 30 minutes of medication, the
                            temperature hasn’t gone down, a Doctor must be called to examine the resident.</li>
                        <li>If the temperature is 39-40 degrees, you should administer medication in accordance with the
                            administering medication policy and call an ambulance.</li>
                        <li>Ensure incident, illness, injury and trauma record is completed with all temperatures taken
                            included.</li>
                    </ul>
                    <h3><span style="color: #fcb53b;">Methods to Reduce a Temperature:</span></h3>
                    <ul>
                        <li>Encourage the person to drink plenty of water unless there are reasons why the person is only
                            allowed limited fluids.</li>
                        <li>Remove excessive clothing (shoes, socks, jumpers, pants, etc.). Staff will need to be mindful of
                            cultural beliefs.</li>
                        <li>Sponge lukewarm water on the person’s forehead, back of the neck and exposed areas of skin.</li>
                    </ul>
                    <h3><span style="color: #fcb53b;">Colds/Flu (Running Nose)</span></h3>
                    <p>It is important to determine if you are dealing with a cold and not Covid 19. Covid 19 can be
                        life-threatening to the elderly or immunocompromised, so a PCR test must be performed in the first
                        instance.</p>
                    <p>Colds are the most common cause of illness in both children and adults. Staff and residents often
                        present with symptoms that relate to colds and flu. These symptoms are often spread quickly and
                        easily throughout the service, and it’s important that staff wash their hands and implement cleaning
                        practices. With different strains, this takes extra vigilant care and practices to prevent and
                        minimise spread.</p>
                    <p>Symptoms include:</p>
                    <p>Runny or blocked nose – if the discharge is green, consult a Doctor. Ensure the resident uses a
                        tissue to blow nose and washes hands after. For staff assisting residents, use gloves and tissue and
                        wash hands after. Discard tissue after one use.</p>
                    <p>Encourage the resident to cough/sneeze into their elbow and move away from people. Clean hands if
                        coughing into hands. Clean any areas that a sneeze/cough has made contact with.</p>
                    <p>Watery eyes – monitor person, clear eye with a tissue, discard and wash hands after. If a green
                        discharge occurs, wipe the eye with warm water. If discharge comes back, consult a Doctor.</p>
                    <p>Headache – create a quiet space for the resident, offer water and bed to rest. Call doctor if
                        headache persists.</p>
                    <p>Mild sore throat – monitor for other symptoms, give resident water and easy-to-swallow foods. Be more
                        vigilant about cleaning any materials the person comes into contact with.</p>
                    <h3><span style="color: #fcb53b;">Diarrhoea and Vomiting (Gastroenteritis)</span></h3>
                    <p>Gastroenteritis (or ‘gastro’) is a general term for an illness of the digestive system. There are
                        different medical reasons for diarrhoea and vomiting – some are threatening to others and some are
                        not. In elderly residents, dehydration can occur quickly, so medical attention should be sought.</p>
                    <p>Typical reasons for vomiting and diarrhoea that are not spreadable:</p>
                    <ul>
                        <li>antibiotics</li>
                        <li>food poisoning</li>
                        <li>intolerance/allergies</li>
                        <li>headache.</li>
                    </ul>
                    <p>Gastroenteritis is extremely spreadable to others and has the same symptoms as above.</p>
                    <p>Typical symptoms:</p>
                    <ul>
                        <li>abdominal cramps</li>
                        <li>diarrhoea</li>
                        <li>vomiting</li>
                        <li>pale</li>
                        <li>lethargic</li>
                        <li>not eating or drinking water.</li>
                    </ul>
                    <h3><span style="color: #fcb53b;">Rashes</span></h3>
                    <p>Rashes can occur for many reasons, and it can be hard to determine the underlining factors. Some
                        rashes can be harmless to people, whereas others can be highly infectious. As it’s very hard to
                        determine, staff should treat all rashes with the same practices unless advised otherwise by a
                        Doctor.</p>
                    <p>Typical reasons for non-infectious rashes:</p>
                    <ul>
                        <li>allergies and food reactions or insect bites</li>
                        <li>skin diseases such as eczema</li>
                        <li>heat rash.</li>
                    </ul>
                    <p>However, rashes can be a sign of infectious communicable disease and should be taken with high
                        precaution.</p>
                    <p>Typical infectious diseases that display rashes:</p>
                    <ul>
                        <li>Ringworm</li>
                        <li>Impetigo</li>
                        <li>Staphylococcus</li>
                        <li>Chickenpox/Shingles</li>
                        <li>Measles/Rubella.</li>
                    </ul>
                    <p>Treatment of rashes:</p>
                    <ul>
                        <li>When a rash is identified with no known previous reason, staff should respectfully isolate the
                            resident until a medical opinion is sought.</li>
                        <li>Monitor the rash and the person’s health.</li>
                        <li>Complete the illness, incident, trauma and illness record form and submit to management.</li>
                    </ul>
                    <h2><span style="color: #da591f;">Incident, Injury, Trauma</span></h2>
                    <h3><span style="color: #fcb53b;">Serious Incident procedure</span></h3>
                    <p>The Serious Incident Procedure has the following aims:</p>
                    <ul>
                        <li>to reduce the risk of abuse and neglect for Banksia Care clients</li>
                        <li>to ensure that any person who suffers from abuse or neglect receives immediate health care,
                            including counselling</li>
                        <li>to ensure that any person who alleges abuse or neglect or suffers from abuse or neglect has the
                            right to a fair and transparent legal process.</li>
                    </ul>
                    <h4><span style="color: #b67318;">A serious incident is defined as any of the following:</span></h4>
                    <ol>
                        <li>Unreasonable use of force</li>
                        <li>Unlawful sexual contact or inappropriate contact</li>
                        <li>Neglect</li>
                        <li>Psychological or emotional abuse</li>
                        <li>Unexpected death</li>
                        <li>Stealing or financial coercion by a staff member</li>
                        <li>Inappropriate use of restrictive practices</li>
                        <li>Unexplained absence from care</li>
                    </ol>
                    <h4><span style="color: #b67318;">Reporting procedures for serious incidents:</span></h4>
                    <p>Serious incidents must be reported according to the following procedures:</p>
                    <ul>
                        <li><strong>Verbally </strong>reported to the shift supervisor <strong>immediately</strong></li>
                        <li>Reported to the shift supervisor <strong>in writing</strong> <strong>within 12 hours</strong> of
                            the alleged incident using the Serious Incident Report Form</li>
                        <li>Reported to the Police within 24 hours</li>
                        <li>In the case of Aged Care clients, reported to the Aged Care Quality and Safety Commission
                            <strong>within 24hrs</strong></li>
                        <li>In the case of clients with a disability, reported to the NDIS Quality and Safeguards Commission
                            <strong>within 24 hours </strong>of the alleged incident</li>
                    </ul>
                    <p>Any deviation from these procedures by a staff member may result in instant dismissal.</p>
                    <ul></ul>
                    <h2><strong><span style="color: #da591f;">SERIOUS INCIDENT REPORT GUIDELINES</span></strong></h2>
                    <p>In response to any allegation, evidence or suspicion of abuse or neglect, Banksia Care employees,
                        contractors and volunteers must complete a Serious Incident Report and submit it to the supervisor
                        <strong><em>within12 hours</em></strong>.</p>
                    <p>As this is a legal document, all necessary information must be included in the Serious Incident
                        Report, including the date, time, location, names of any persons involved (including witnesses), and
                        all factual details about the alleged or suspected abuise or neglect.</p>
                    <p>Include only objective facts, specifically:</p>
                    <ul>
                        <li>events you witnessed</li>
                        <li>events reported to you by a client</li>
                        <li>events witnessed and reported to you by a third party</li>
                        <li>clear physical evidence (e.g. bruising, torn underwear)</li>
                    </ul>
                    <p>Conjecture, gossip and unfounded allegations <strong><em>must not</em></strong> be included in a
                        Serious Incident Report.</p>
                    <p>When filling in a Serious Incident Report, your personal feelings or judgements <strong><em>must
                                not</em></strong> be included.</p>
                    <p><em>Refer to the ‘Whistleblowers Protection Policy and Procedure’ and ‘Mandatory Reporting Policy’
                            and Procedure’ for further information on staff rights and responsibilities. </em></p>
                    <p><em>Refer to the ‘Administration of Medication Policy and Procedure’ for further information on
                            incidents involving medication. </em></p>
                    <h3><span style="color: #fcb53b;">Injury</span></h3>
                    <p>Refers to any event that happens within the service that causes harm, for example, falling off any
                        equipment, break or sprain resulting from an activity. All injuries or incidents, whether serious or
                        non-serious, should be assessed and reviewed for safer practices to implement.</p>
                    <h4><span style="color: #b67318;">When an injury/incident occurs (deemed non-serious):</span></h4>
                    <ul>
                        <li>Ensure the area is safe and free from harm, and remove resident/s and staff if needed.</li>
                        <li>Administer first aid to the injured person – if not serious, allow a person to continue with the
                            day and monitor the person.</li>
                        <li>Complete the Incident Report Form.</li>
                    </ul>
                    <h4><span style="color: #b67318;">Safe Manual Handling/Lifting</span></h4>
                    <p>Performing manual handling is almost certain when working in aged or disability care. Many day-to-day
                        tasks will include one or more of the following:</p>
                    <ul>
                        <li>assisted walking</li>
                        <li>pushing a wheelchair</li>
                        <li>helping a resident to sit or stand</li>
                        <li>requirement to lift, support or relocate a resident</li>
                        <li>move a bed, trolley, wheelchair or other medical equipment, or adjust such.</li>
                    </ul>
                    <p>There are some common practices that are the cause of injuries in staff whose role involves manual
                        handling:</p>
                    <ol>
                        <li>lifting incorrectly</li>
                        <li>incorrect posture when handling residents</li>
                        <li>adjusting or relocating equipment, such as beds, incorrectly</li>
                        <li>attempting to or lifting items/people that are too heavy</li>
                        <li>trying to deal with a stressed, violent or confused resident alone.</li>
                    </ol>
                    <h4><span style="color: #b67318;">Lifting:</span></h4>
                    <p>With abdominal muscles engaged and your spine straight, always lift from a semi-squat position. In
                        executing lifts, the force must always come from the legs, not your back.</p>
                    <h4><span style="color: #b67318;">Pushing:</span></h4>
                    <p>It is a common rule that pushing is far safer than lifting or pulling a heavy object. Use your body
                        weight and a wide base of support to push, keeping elbows close to your waist and initiate movement
                        from lower limbs.</p>
                    <h4><span style="color: #b67318;">Pulling:</span></h4>
                    <p>Distribute body weight evenly through the placement of feet and use lower limbs to initiate movement.
                        Use both arms, facing the object straight on to pull.</p>
                    <h3><span style="color: #fcb53b;">Trauma</span></h3>
                    <p>Trauma defines the impact of an event or a series of events during which a person feels helpless and
                        pushed beyond their ability to cope. There are various events that might be traumatic to a person,
                        including accidents, injuries, serious illness, natural disasters, pandemics, war, terrorist
                        attacks, assault, threats of violence, domestic violence, neglect or abuse. Carer or cultural trauma
                        can also have a traumatising influence on staff and residents. This trauma could occur in or outside
                        the service for staff or residents.</p>
                    <p>In relation to residents:</p>
                    <ul>
                        <li>Staff should monitor and observe residents’ behaviour and report to management.</li>
                        <li>Offer briefing and professional counselling in trauma that occurred within the service.</li>
                        <li>Seek professional help for the trauma that did not occur within the service.</li>
                        <li>Follow the guidance of specialists to support trauma-affected residents.</li>
                        <li>Identify a supportive person to talk to about your experiences. This might be your doctor or
                            another health professional.</li>
                        <li>Living or working with traumatised residents can be demanding – be aware of your own responses
                            and seek support from management when required.</li>
                        <li>Where required complete the Incident Report Form and contact the authorities within your
                            jurisdiction.</li>
                    </ul>
                    <p>In relation to staff:</p>
                    <ul>
                        <li>Report any concerns for yourself or another staff member in relation to trauma. This could be
                            noticing a change in attitude or behaviour.</li>
                        <li>Provide adequate time off work to recover and manage trauma.</li>
                        <li>Refer the person to professional health services.</li>
                        <li>Continue to monitor and support the staff member.</li>
                    </ul>

