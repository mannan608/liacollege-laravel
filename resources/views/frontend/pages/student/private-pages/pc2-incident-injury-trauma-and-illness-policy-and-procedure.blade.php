                    <ul>
                        <li>All staff will receive relevant and up-to-date training to ensure they can effectively respond
                            to incidents, injuries, trauma and illness.</li>
                        <li>Emergency services will be contacted if the assessment of the incident determines the need.</li>
                        <li>Families/next of kin of the client will be notified of any serious incident as soon as possible.
                        </li>
                        <li>Staff are not doctors and cannot diagnose an illness or infectious disease of staff or
                            residents. We will refer the resident to receive medical advice.</li>
                        <li>This policy and procedure does not intend to cover all illnesses that can occur in an aged care
                            or disability service or within independent living arrangements. It serves to identify some
                            common illnesses and guide staff on initial responses to such.</li>
                    </ul>
