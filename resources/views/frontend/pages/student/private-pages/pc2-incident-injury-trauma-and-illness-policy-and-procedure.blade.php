                        <li>Implement prevention and responsive practices when required.</li>
                        <li>Exclude themselves from the service when unwell, injured or unable to perform their duties
                            safely.</li>
                        <li>Be up-to-date with immunisations.</li>
                        <li>Report accidents, illness or injuries to themselves or other staff to management immediately.
                        </li>
                        <li>Be open to professional development and ongoing training.</li>
                        <li>Complete relevant documentation regarding illness, accident and trauma.</li>
                        <li>Share relevant and current health information with management.</li>
                        <li>Ensure appropriate cleaning and hygiene practices are being implemented.</li>
                    </ul>
                    <p>In Relation to Resident’s Health and Safety:</p>
                    <ul>
                        <li>Observe and be aware of the resident’s current health and act quickly in response.</li>
                        <li>Consult with families in regard to the resident’s current health needs.</li>
                        <li>Assess the environment for safety – use relevant checklists.</li>
                        <li>Educate residents about safety.</li>
                        <li>Clean hands of both staff and residents.</li>
                        <li>Clean the environment adequately and regularly.</li>
