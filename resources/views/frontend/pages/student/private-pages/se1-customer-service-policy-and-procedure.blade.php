                    <h2><span style="color: #da591f;">Customer Service Procedure</span></h2>
                    <ul></ul>
                    <h3><span style="color: #fcb53b;">Face-to-Face Customers:</span></h3>
                    <ul>
                        <li>Regularly consult with the team wherever practical on all WHS issues and regulatory or workplace
                            changes that may affect their workplace.</li>
                        <li>Welcome all individuals as they enter the facility or as you walk past with ‘Good
                            morning/afternoon’ in a polite tone of voice and with a genuine smile.</li>
                        <li>Residents should be acknowledged within 30 seconds of them entering Banksia Care.</li>
                        <li>Try and engage the patients, residents, carers and family in a conversation where possible and
                            appropriate.</li>
                        <li>In a friendly, professional manner, always let patients, residents, carers and family know you
                            are there to help. If you are busy with another person, advise that you will assist them shortly
                            or ask another staff member to assist them.</li>
                        <li>You should use a combination of open, closed, and reflective questioning techniques to find out
                            exactly what each individual needs.</li>
                        <li>Your farewell statement should be warm, friendly, and genuine.</li>
                        <li>Be always polite and friendly.</li>
                    </ul>
                    <ul></ul>
                    <h3><span style="color: #fcb53b;">Telephone Etiquette</span></h3>
                    <p>Patients, residents, carers and family on the telephone are just as important as those whom we
                        encounter face-to-face. They should be treated with the same priority and respect.</p>
                    <ul>
                        <li>The phone must be answered within two rings where possible.</li>
                        <li>The standard greeting will be: ‘Thank you for calling Banksia Care, ________ speaking. How may I
                            help you?’</li>
                        <li>Actively listen to the callers’ requests and assist accordingly.</li>
                    </ul>
                    <p>If you cannot assist the caller, direct the call to the appropriate person. Ensure the caller knows
                        that you are transferring their call and give them the name of the person you are transferring them
                        to.</p>
                    <ul>
                        <li>If you need to place a call on hold, please ask permission from the caller to do this.</li>
                        <li>If the person they require is not on-site at the time, a message slip must be filled out and
                            personally given to the staff member when they return to the facility.</li>
                        <li>End the conversation in a courteous and professional way by thanking the caller.</li>
                        <li>Any voicemails should be dealt with within 24 hours during normal business hours.</li>
                    </ul>
                    <ul></ul>
                    <h3><span style="color: #fcb53b;">Diverse Customers</span></h3>
                    <p>Banksia Care has residents and patients from diverse backgrounds, including those with physical and
                        intellectual disabilities, non-English speaking backgrounds, and diverse cultural beliefs. All
                        employees will treat every customer with dignity and respect, no matter the situation.</p>
                    <p>Some points to remember when dealing with diverse customers are:</p>
                    <ul>
                        <li>avoid using jargon</li>
                        <li>ask the other person if they understand what you are saying</li>
                        <li>speak clearly</li>
                        <li>listen carefully to what is said</li>
                        <li>use gestures, drawings, demonstrations, and signs to aid understanding.</li>
                    </ul>
