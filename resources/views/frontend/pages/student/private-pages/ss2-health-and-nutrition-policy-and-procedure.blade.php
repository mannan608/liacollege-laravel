                    <h2><span style="color: #da591f;">Health and Nutrition Procedure</span></h2>
                    <ul></ul>
                    <h3><span style="color: #fcb53b;">Management will be responsible for the following:</span></h3>
                    <ul>
                        <li>Ensure staff undergo Food Safety training.</li>
                        <li>Implement the health and nutrition policy.</li>
                        <li>Ensure all clinicians are consistent in implementing any nutrition plan and their instructions
                            align with the policy.</li>
                        <li>Ensure all Banksia Care staff incorporate the goals of this and other related policies into any
                            food-related activity, event, incursion or excursion.</li>
                        <li>Train staff in identifying early signs of malnutrition or obesity in clients or residents being
                            admitted to care.</li>
                        <li>Document and manage early identification of malnutrition or obesity.</li>
                        <li>Provide opportunities and resources for lifestyle activities relevant to health and wellbeing.
                        </li>
                        <li>Provide opportunities for cultural and religious events to be celebrated with food.</li>
                        <li>Maintain an inviting dining room and resources for individuals if mealtime is to take place in
                            their own room.</li>
                        <li>Review and report any nutritional care incidents.</li>
                        <li>Develop a process for introducing and assessing new food products, packaging, and utensils for
                            ease of accessibility and suitability to the clients’/residents’ needs at Banksia Care.</li>
                    </ul>
                    <h3><span style="color: #fcb53b;">Staff will ensure the following:</span></h3>
                    <ul>
                        <li>Understand they have a duty of care to ensure the nutritional needs of clients and residents are
                            met.</li>
                        <li>Ensure they regularly conduct weight assessments and nutrition screening to develop individual
                            action plans.</li>
                        <li>Complete Banksia Care training session on Food Safety before commencing employment.</li>
                        <li>Provide any aids and support required to assist residents with eating.</li>
                        <li>Encourage self help in eating as a way to maintain independence.</li>
                        <li>Cater to individuals’ cultural and religious needs in regard to meal options, routines, rituals
                            and practices.</li>
                        <li>Provide an opportunity for feedback on menu planning.</li>
                        <li>Allow opportunity for help from clients and residents in meal prep or gardening in the communal
                            garden.</li>
                        <li>Engage residents and clients in their own health and nutrition plans.</li>
                        <li>Encourage client and resident participation in any physical activity they can partake in that
                            will improve physical and mental health.</li>
                        <li>Ensure all meals are pleasing and appetising in regard to presentation.</li>
                        <li>Make every effort to ensure mealtime environments are conducive to eating, whether one on one or
                            in the dining room.</li>
                        <li>Treat each client/resident respectfully and ensure adequate time is given to complete a meal.
                        </li>
                    </ul>
                    <h3><span style="color: #fcb53b;">In Relation to Individualised Plans and their Connection to Health and
                            Nutrition</span></h3>
                    <ul>
                        <li>An initial Individualised Plan will be developed reflecting the needs and aspirations of the
                            person and/or family and the support required to meet those needs. This will involve identifying
                            any dietary, cultural or religious needs in relation to food and drink.</li>
                        <li>One or more planning meetings will be coordinated to develop the plan. Meetings will be at times
                            and venues convenient to everyone involved to maximise the participation of key people. If the
                            client/resident is deemed to have any nutritional risk, key people will include the
                            dietician/nutritionist, doctor and chef.</li>
                    </ul>
                    <h3><span style="color: #fcb53b;">Content of the Plan and Relevance to Health and Nutrition:</span></h3>
                    <p>The individualised plan will include goals (and support required) for each of the following:</p>
                    <ul>
                        <li>health and wellbeing (weight, physical health, mental health)</li>
                        <li>participation (meal time)</li>
                        <li>independent living skills development (preparing own meals, making healthy choices, shopping)
                        </li>
                        <li>engagement in the local community (communal garden, dining room, social events)</li>
                        <li>recreational activities at home or in the community (cooking, gardening, eating out, picnics)
                        </li>
                        <li>forming friendships and peer networks (cooking classes, eating out socially or together
                            in-house)</li>
                        <li>managing finances, material possessions and/or accumulating savings (shopping budgets)</li>
                        <li>self-expression including clothing, appearance (appropriate to their age) (food preferences)
                        </li>
                        <li>exploring different lifestyle choices (relating to food, exercise etc.).</li>
                    </ul>

