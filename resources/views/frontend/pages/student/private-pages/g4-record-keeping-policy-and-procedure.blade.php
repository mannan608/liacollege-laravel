                    <h2><span style="color: #da591f;">Record Keeping Procedure</span></h2>
                    <ul></ul>
                    <h3><span style="color: #fcb53b;">Management will ensure the following:</span></h3>
                    <ul>
                        <li>Ensure confidential information at Banksia Care is only transferred from one person to another
                            when it is professionally appropriate and legally acceptable to do so.</li>
                        <li>Remind staff regularly about workplace confidentiality and professional expectations, as well as
                            privacy obligations as detailed in the Information Privacy Act.</li>
                        <li>Confidential information relating to any current or past resident, family or staff member will
                            be maintained in individual files in the office and can only be accessed by authorised
                            personnel.</li>
                        <li>Maintain a register for staff to sign when accessing confidential files.</li>
                        <li>Ensure discussions about a resident are not held outside of the service with a third person or
                            agency which has no legal or compelling need to discuss such issues.</li>
                        <li>Be mindful of confidential information on desks, notice boards, and computers and ensure it is
                            not visible to other residents or visitors.</li>
                        <li>Delegate responsibility for monitoring administrative record keeping practices.</li>
                        <li>Ensure information is collected and electronically using the existing database system and in
                            accordance with the Privacy Act.</li>
                        <li>Thoroughly investigate any alleged breach of confidentiality or privacy.</li>
                    </ul>
                    <h3><span style="color: #fcb53b;">Staff will ensure the following:</span></h3>
                    <ul>
                        <li>Understand the limits of the confidentiality they can offer to both residents and families, in
                            particular students and volunteers.</li>
                        <li>Seek advice from the Care Coordinator if a resident discusses a difficult personal matter.</li>
                        <li>Be mindful of confidential information on desks, notice boards, and computers and ensure it is
                            not visible to other residents or visitors.</li>
                        <li>Use correct forms for recording information related to residents and store it in correct
                            location.</li>
                    </ul>
                    <h3><span style="color: #fcb53b;">All Banksia Care staff will record:</span></h3>
                    <ul>
                        <li>All assessments of care recipients</li>
                        <li>Individual care plans for every care recipient</li>
                        <li>Medical records, progress notes, and any other clinical records</li>
                        <li>Any letters from referred healthcare providers</li>
                        <li>Agreements between the care recipient, Banksia Care, and other healthcare providers</li>
                        <li>Up-to-date details of any representatives of care recipients.</li>
                    </ul>
                    <h3><span style="color: #fcb53b;">In Relation to Collection of Information</span></h3>
                    <ul>
                        <li>In general, personal information (including health information) may be collected from a resident
                            and their family; any person or organisation that assesses health status or care requirements,
                            for example, the aged care staff, other facilities; family members or significant persons of a
                            resident, and from a legal advisor of a resident and their family.</li>
                        <li>Banksia Care will collect personal information directly from a resident unless:
                            <ul>
                                <li>we have your consent to collect the information from someone else;</li>
                                <li>we are required or authorised by law to collect the information from someone else; or
                                </li>
                                <li>it is unreasonable or impractical to do so.</li>
                            </ul>
                        </li>
                        <li>The types of personal information collected include:
                            <ul>
                                <li>personal information provided by you, including your name, date of birth, telephone
                                    number and next of kin;</li>
                                <li>health and financial information, in the event that you enter our care;</li>
                                <li>Government identifiers such as Medicare, Pension or Veteran’s Affairs numbers;</li>
                                <li>information that we obtain about you in the course of your interaction with our website,
                                    including your internet protocol (IP) address, the date and time of your visit to our
                                    website, the pages you have accessed, the links on which you have clicked and the type
                                    of browser that you were using; and</li>
                                <li>aggregated statistical data, which is information relating to your use of our website
                                    and our services, such as traffic flow and demographics.</li>
                            </ul>
                        </li>
                    </ul>
                    <h3><span style="color: #fcb53b;">In Relation to Storage and Use of Information</span></h3>
                    <ul>
                        <li>Banksia Care will store all records containing personal information.</li>
                        <li>Banksia Care will only use the personal information held about residents to enable efficient
                            administration of services provided, reporting data to provide information about trends and
                            opportunities, and maintaining accurate and detailed records of business interactions and
                            outcomes.</li>
                        <li>Banksia Care may use the personal information provided by an individual to market other internal
                            services to them. An individual may opt-out of being contacted for marketing purposes at any
                            time by contacting our office. Information will not be passed onto any third-party marketing
                            companies without the individual’s prior written consent.</li>
                    </ul>
                    <h3><span style="color: #fcb53b;">In Relation to Disclosure of Information</span></h3>
                    <ul>
                        <li>Banksia Care will not disclose an individual’s personal information to another person or
                            organisation unless they are aware that information of that kind is usually passed to that
                            person or organisation.</li>
                        <li>Personal information may be used or disclosed by Banksia Care for statistical, regulatory and
                            research purposes.</li>
                    </ul>
                    <h3><span style="color: #fcb53b;">In Relation to Access to and Correction of Records</span></h3>
                    <ul>
                        <li>Individuals have the right to access or obtain a copy of the information Banksia Care holds
                            about them, including personal details, next of kin, and any other information.</li>
                        <li>Requests to access or obtain a copy of the records held about residents must be made by
                            contacting our office using the Request to Access Records Form. The individual must prove their
                            identity to be able to access their records.</li>
                        <li>There is no charge for an individual to access the records that Banksia Care holds about them;
                            however, there may be a charge for any copies made. Arrangements will be made within ten days
                            for the individual to access their records.</li>
                    </ul>
                    <h3><span style="color: #fcb53b;">In Relation to Accessing Records</span></h3>
                    <ul>
                        <li>Individuals may request to access their records using the Request to Access Records Form.
                            Written requests should be sent to the Residential Manager.</li>
                        <li>Upon receiving a completed form, confirm the request is valid and has been made by the
                            individual to which the records relate – check identification documents.</li>
                        <li>Arrangements for the provision of records should be made as suitable – mailing copies, providing
                            a time for records to be viewed, etc.</li>
                        <li>Arrangements should be made verbally and confirmed in writing within ten days of receiving the
                            request.</li>
                        <li>Keep a note of how the records were accessed on the individual’s file.</li>
                    </ul>
                    <h3><span style="color: #fcb53b;">In Relation to Amendment of Records</span></h3>
                    <ul>
                        <li>When an individual request is made for an incorrect record held about them to be corrected, they
                            can do so by filling in an Amendment to Records Request Form.</li>
                        <li>If it is a change of contact person or mobile phone, they can use the Change of Details Form.
                        </li>
                        <li>Upon receipt of a request form, consider whether the records held are correct or not. If the
                            request is valid and records are incorrect, update records accordingly.</li>
                        <li>Do not update records if they are found to be correct already.</li>
                        <li>Advise the resident or family member accordingly of the actions taken to follow up on their
                            request.</li>
                    </ul>

